CREATE FUNCTION `FN_CHECK_AGREMENT_ATT_TYP`(   
    AV_MODULE_ITEM_KEY INT,
    AV_AGREEMENT_ATTACHMENT_TYPE_CODE INT
) RETURNS varchar(10) 
    DETERMINISTIC
BEGIN
    DECLARE LI_COUNT INT default 0;

    SELECT COUNT(*) INTO LI_COUNT
    FROM agreement_attachment t1
    WHERE t1.AGREEMENT_REQUEST_ID = AV_MODULE_ITEM_KEY
    AND t1.AGREEMENT_ATTACHMENT_TYPE_CODE = AV_AGREEMENT_ATTACHMENT_TYPE_CODE;


    IF LI_COUNT > 0 THEN
       RETURN 'TRUE';
    ELSE
        RETURN 'FALSE';
     END IF;
END
