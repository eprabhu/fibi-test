CREATE TRIGGER RESTRICT_UPDATE_PARAMETER
BEFORE UPDATE 
ON PARAMETER
FOR EACH ROW
BEGIN
    IF NEW.PARAMETER_NAME = 'EMAIL_NOTIFICATION_TEST_ENABLED' AND NEW.VALUE = 'N' THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Updating the VALUE column to "N" is not allowed for PARAMETER_NAME = \'EMAIL_NOTIFICATION_TEST_ENABLED\'.';
    END IF;
END
