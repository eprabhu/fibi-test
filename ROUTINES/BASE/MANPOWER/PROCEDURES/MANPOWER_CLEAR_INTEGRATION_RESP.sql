-- `MANPOWER_CLEAR_INTEGRATION_RESP`; 

CREATE PROCEDURE `MANPOWER_CLEAR_INTEGRATION_RESP`()
BEGIN
 
UPDATE MANPOWER_LOG SET RESPONSE = NULL WHERE INTERFACE_TYPE_CODE IN (2, 18) AND UPDATE_TIMESTAMP < (date_add(NOW(), INTERVAL -30 DAY));
UPDATE MANPOWER_LOG SET RESPONSE = NULL WHERE INTERFACE_TYPE_CODE IN (11) AND UPDATE_TIMESTAMP < (date_add(NOW(), INTERVAL -7 DAY));
 
END
