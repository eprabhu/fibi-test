CREATE PROCEDURE `GET_AGRMENT_DASHBOARD_JSON_CNT`(
JSON_FORMAT JSON
)
BEGIN

	DECLARE AV_AGREEMENT_REQUEST_ID varchar(20);
	DECLARE AV_TITLE                varchar(1000);
	DECLARE AV_REQUESTOR_PERSON_ID    varchar(40);
	DECLARE AV_TYPE_CODE            VARCHAR(255);
	DECLARE AV_STATUS_CODE          VARCHAR(255);
	DECLARE AV_PERSON_ID            VARCHAR(255);
	DECLARE AV_SORT_TYPE            VARCHAR(255);
	DECLARE AV_PAGED                INT;
	DECLARE AV_LIMIT                INT;
	DECLARE AV_TAB_TYPE             VARCHAR(255);
	DECLARE AV_UNLIMITED            VARCHAR(255);
	DECLARE AV_TYPE                 VARCHAR(255);
	DECLARE AV_UNIT_NUMBER			VARCHAR(255);
	DECLARE AV_ORGANIZATION 		VARCHAR(255);
	DECLARE AV_PI_PERSON_ID			VARCHAR(255);
	DECLARE AV_REVIEW_STATUS		VARCHAR(255);
	DECLARE AV_ORGANIZATION_TYPE 	VARCHAR(255);
	DECLARE AV_ADMIN_PERSON_ID 		VARCHAR(255);
	DECLARE AV_START_DATE 		    VARCHAR(255);
	DECLARE AV_END_DATE		        VARCHAR(255);
	DECLARE AV_PROJECT_ID	        VARCHAR(255);
	DECLARE AV_KEYWORD              VARCHAR(200);
	DECLARE LS_IS_ADMIN_ASSIGNED 	VARCHAR(1);
	DECLARE LS_ADMIN_PERSON_IDS		LONGTEXT;
    DECLARE LS_ASSIGNEE_IDS			LONGTEXT;
	DECLARE LS_CATEGORY				LONGTEXT;
	DECLARE LS_LOCATION_STATUS 		LONGTEXT;
	DECLARE LS_DESCEND_FLAG       VARCHAR(1);
	DECLARE LS_WIDGET_UNIT_NUMBER      VARCHAR(255);

	DECLARE LS_DYN_SQL 				LONGTEXT;
	DECLARE LS_FILTER_CONDITION 	LONGTEXT;
	DECLARE LS_OFFSET_CONDITION 	VARCHAR(600);
	DECLARE LS_OFFSET 				INT(11);
	DECLARE LS_TAB_QUERY 			LONGTEXT;
	DECLARE JOIN_CONDITION 		    LONGTEXT;
	DECLARE LS_SELECTED_FIELD_LIST 	LONGTEXT;
	DECLARE LS_FILTER_CONDITION1 	LONGTEXT;
	DECLARE LS_FILTER_CONDITION2 	LONGTEXT;
	DECLARE LS_FILTER_CONDITION3 	LONGTEXT;
	DECLARE LS_FILTER_CONDITION4 	LONGTEXT;
	DECLARE LS_FILTER_CONDITION5 	LONGTEXT;
	DECLARE LS_FILTER_CONDITION_NEW		LONGTEXT;
	DECLARE LS_SORT_TYPE            VARCHAR(1000);
	DECLARE IS_UNIT_ADMIN INT(11);
	DECLARE IS_DEPT_ADMIN INT(11);
	DECLARE IS_GROUP_ADMIN INT(11);
	DECLARE LS_ADMIN_GROUP_CONDITION LONGTEXT;
	DECLARE LS_FILTER_TEMPLATE_CONDITION 	LONGTEXT;

	-- CUSTOM DATA
	DECLARE KEY_PREFIX VARCHAR(255);
	DECLARE LS_CUSTOM_DATA_CONDITION VARCHAR(1000) DEFAULT ''; 
	DECLARE LS_CUSTOM_DATA_SELECT VARCHAR(1000) DEFAULT ''; 
	DECLARE LS_CUSTOM_DATA_SEL VARCHAR(1000) DEFAULT ''; 
	DECLARE LS_CUSTOM_DATA_JOIN VARCHAR(1000) DEFAULT ''; 

	DECLARE LS_CLAUSE VARCHAR(10) DEFAULT ''; 
	DECLARE NOT_FOUND INT;
	DECLARE LI_CUSTOM_DATA_ELEMENTS_ID INT;
	DECLARE LI_DATA_TYPE INT;
	DECLARE LI_LOOP_COUNT INT DEFAULT 0;
	--  DECLARE KEY_PREFIX_COUNT VARCHAR(2000);
	DECLARE STR_LEN INT DEFAULT 0;
	--  DECLARE PRE_VAL VARCHAR(255);
	DECLARE KEY_PREFIX_STRING VARCHAR(2000);

	DECLARE CUSTOM_DATA_ELEMENTS_IDS CURSOR FOR
	  SELECT CUSTOM_DATA_ELEMENTS_ID, DATA_TYPE FROM CUSTOM_DATA_ELEMENTS WHERE CUSTOM_DATA_ELEMENTS_ID IN
	  (SELECT CUSTOM_DATA_ELEMENTS_ID FROM CUSTOM_DATA_ELEMENT_USAGE WHERE MODULE_CODE = 13
		AND IS_ACTIVE = 'Y' AND IS_REQ_ADVANCE_SEARCH = 'Y'
	);
  
	DECLARE CONTINUE HANDLER FOR NOT FOUND  SET NOT_FOUND =1;
	SET SESSION TRANSACTION ISOLATION LEVEL READ COMMITTED;
	SET AV_AGREEMENT_REQUEST_ID = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.AGREEMENT_REQUEST_ID'));
	SET AV_TITLE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.TITLE'));
	SET AV_REQUESTOR_PERSON_ID = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.REQUESTOR_PERSON_ID'));
	SET AV_TYPE_CODE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.TYPE_CODE'));
	SET AV_STATUS_CODE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.STATUS_CODE'));
	SET AV_PERSON_ID = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.PERSON_ID'));
	SET AV_SORT_TYPE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.SORT_TYPE')); 
	SET AV_PAGED = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.PAGED'));
	SET AV_LIMIT =  JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.LIMIT'));
	SET AV_TAB_TYPE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.TAB_TYPE'));	
	SET AV_UNLIMITED =  JSON_EXTRACT(JSON_FORMAT,'$.UNLIMITED');
	SET AV_TYPE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.TYPE'));
	SET AV_UNIT_NUMBER = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.UNIT_NUMBER'));
	SET AV_ORGANIZATION = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.ORGANIZATION'));
	SET AV_PI_PERSON_ID = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.PI_PERSON_ID'));
	SET AV_REVIEW_STATUS = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.REVIEW_STATUS'));
	SET AV_ORGANIZATION_TYPE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.ORGANIZATION_TYPE'));
	SET AV_ADMIN_PERSON_ID = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.ADMIN_PERSON_ID'));
	SET AV_START_DATE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.START_DATE'));
	SET AV_END_DATE = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.END_DATE'));
	SET AV_PROJECT_ID = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.PROJECT_ID'));
	SET AV_KEYWORD = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.KEYWORD'));
	SET LS_ASSIGNEE_IDS = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.ASSIGNEE_IDS'));
	SET LS_ADMIN_PERSON_IDS = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.ADMIN_PERSON_IDS'));
	SET LS_IS_ADMIN_ASSIGNED = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.IS_ADMIN_ASSIGNED'));
	SET LS_CATEGORY = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.CATEGORY'));
	SET LS_LOCATION_STATUS = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.LOCATION_STATUS'));
	SET LS_DESCEND_FLAG = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.DESCEND_FLAG'));
	SET LS_WIDGET_UNIT_NUMBER = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT,'$.WIDGET_UNIT_NUMBER'));

	SET LS_CUSTOM_DATA_SELECT = '';
	SET LS_CUSTOM_DATA_SEL = '';
	
	-- CUSTOM DATA
	OPEN CUSTOM_DATA_ELEMENTS_IDS;
	CUSTOM_DATA_ELEMENTS_IDS:LOOP
		 
		SET NOT_FOUND = 0;
		FETCH CUSTOM_DATA_ELEMENTS_IDS INTO LI_CUSTOM_DATA_ELEMENTS_ID,LI_DATA_TYPE;

		IF NOT_FOUND = 1 THEN
		 LEAVE CUSTOM_DATA_ELEMENTS_IDS;
		END IF;

		IF  LI_CUSTOM_DATA_ELEMENTS_ID IS NOT NULL THEN
			SET STR_LEN = 0;
			SET STR_LEN = JSON_LENGTH(JSON_FORMAT, CONCAT('$."', LI_CUSTOM_DATA_ELEMENTS_ID,'"'));
			IF STR_LEN > 0 THEN
				SET KEY_PREFIX = ' ';
				SET  LI_LOOP_COUNT = 0;
				   WHILE  LI_LOOP_COUNT < STR_LEN DO

						 SET KEY_PREFIX_STRING = JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT, CONCAT('$.\"', LI_CUSTOM_DATA_ELEMENTS_ID,'\"[',LI_LOOP_COUNT,']')));

						IF KEY_PREFIX_STRING IS NOT NULL THEN
						 SELECT CONCAT(KEY_PREFIX,KEY_PREFIX_STRING,''',''') INTO KEY_PREFIX ;
						END IF;

						SET LI_LOOP_COUNT = LI_LOOP_COUNT + 1;

				  END WHILE;

				SET KEY_PREFIX = LEFT(KEY_PREFIX, LENGTH(KEY_PREFIX) - 3);

			ELSE

			   SET KEY_PREFIX = REPLACE(REPLACE(REPLACE(JSON_UNQUOTE(JSON_EXTRACT(JSON_FORMAT, CONCAT('$."', LI_CUSTOM_DATA_ELEMENTS_ID,'"'))), '[', ''), ']', ''),'"','');

			END IF;

		END IF; -- LI_CUSTOM_DATA_ELEMENTS_ID not  null
		SET KEY_PREFIX = TRIM(KEY_PREFIX);
	
		IF KEY_PREFIX IS NOT NULL  THEN
			SET LS_CUSTOM_DATA_SELECT =CONCAT(LS_CUSTOM_DATA_SELECT,' ,CUST_ELEMENTS_ID_',LI_CUSTOM_DATA_ELEMENTS_ID);
			SET LS_CLAUSE = ' AND (';
			IF LI_DATA_TYPE IN (1,10) THEN
			 SET LS_CUSTOM_DATA_CONDITION = CONCAT( LS_CUSTOM_DATA_CONDITION,LS_CLAUSE, CONCAT(' CUST_ELEMENTS_ID_',LI_CUSTOM_DATA_ELEMENTS_ID),' LIKE  ''%',KEY_PREFIX,'%'')');
			ELSEIF LI_DATA_TYPE IN (4,8,9) THEN
			 SET LS_CUSTOM_DATA_CONDITION = CONCAT( LS_CUSTOM_DATA_CONDITION,LS_CLAUSE, CONCAT(' CUST_ELEMENTS_ID_',LI_CUSTOM_DATA_ELEMENTS_ID),' IN  (''',REPLACE(KEY_PREFIX,',',','),'''))');
			ELSE
			 SET LS_CUSTOM_DATA_CONDITION = CONCAT( LS_CUSTOM_DATA_CONDITION,LS_CLAUSE, CONCAT(' CUST_ELEMENTS_ID_',LI_CUSTOM_DATA_ELEMENTS_ID),' =  ''',KEY_PREFIX,''')');
			END IF;
		END IF; -- KEY_PREFIX IS NOT NULL 
	
	END LOOP CUSTOM_DATA_ELEMENTS_IDS;
	CLOSE CUSTOM_DATA_ELEMENTS_IDS;

	SET LS_FILTER_CONDITION ='';
	SET LS_FILTER_CONDITION_NEW ='';
	SET LS_FILTER_CONDITION1 ='';
	SET LS_FILTER_CONDITION2 ='';
	SET LS_FILTER_CONDITION3 ='';
	SET LS_FILTER_CONDITION4 ='';
	SET LS_FILTER_CONDITION5 	='';
	SET LS_TAB_QUERY 	='';
	SET LS_ADMIN_GROUP_CONDITION ='';
	SET LS_OFFSET = (AV_LIMIT * AV_PAGED);
	SET JOIN_CONDITION = '';
	SET LS_FILTER_TEMPLATE_CONDITION =' T1.AGREEMENT_SEQUENCE_STATUS <> ''TEMPLATE'' ';
	SET LS_FILTER_CONDITION2 = CONCAT(LS_FILTER_CONDITION2,' (T1.UNIT_NUMBER IN(SELECT DISTINCT UNIT_NUMBER FROM ACCESS_TMP_ADMIN)) OR (T1.REQUESTOR_PERSON_ID =''', AV_PERSON_ID ,''') 
																OR (PI.PERSON_ID=''', AV_PERSON_ID ,''') or (T1.ADMIN_PERSON_ID =''',AV_PERSON_ID,''') or (T1.AGREEMENT_REQUEST_ID IN (SELECT R1.AGREEMENT_REQUEST_ID 
																FROM AGREEMENT_PEOPLE_ROLES R1
																INNER JOIN ROLE_RIGHTS R2 ON R1.ROLE_ID = R2.ROLE_ID
																INNER JOIN RIGHTS R3 ON R2.RIGHT_ID = R3.RIGHT_ID 
																WHERE R1.PERSON_ID = ''',AV_PERSON_ID,''' AND R3.RIGHT_NAME IN (''VIEW_ALL_AGREEMENT'',''MODIFY_ALL_AGREEMENT'')))');
																						   
	SET LS_FILTER_CONDITION3 = CONCAT(LS_FILTER_CONDITION3 ,' (T1.UNIT_NUMBER IN(SELECT DISTINCT UNIT_NUMBER FROM ACCESS_TMP)OR (T1.REQUESTOR_PERSON_ID =''', AV_PERSON_ID ,''')
																				  OR (PI.PERSON_ID=''', AV_PERSON_ID ,''')) or (T1.AGREEMENT_REQUEST_ID IN (SELECT R1.AGREEMENT_REQUEST_ID 
																FROM AGREEMENT_PEOPLE_ROLES R1
																INNER JOIN ROLE_RIGHTS R2 ON R1.ROLE_ID = R2.ROLE_ID
																INNER JOIN RIGHTS R3 ON R2.RIGHT_ID = R3.RIGHT_ID 
																WHERE R1.PERSON_ID = ''',AV_PERSON_ID,''' AND R3.RIGHT_NAME IN (''VIEW_ALL_AGREEMENT'',''MODIFY_ALL_AGREEMENT'')))');
	SET LS_FILTER_CONDITION4 = CONCAT(LS_FILTER_CONDITION4 ,' (T1.UNIT_NUMBER IN(SELECT DISTINCT UNIT_NUMBER FROM VIEW_ACCESS_TMP_ADMIN)) OR (T1.REQUESTOR_PERSON_ID =''', AV_PERSON_ID ,''') 
																OR (PI.PERSON_ID=''', AV_PERSON_ID ,''') or (T1.ADMIN_PERSON_ID =''',AV_PERSON_ID,''') or (T1.AGREEMENT_REQUEST_ID IN (SELECT R1.AGREEMENT_REQUEST_ID 
																FROM AGREEMENT_PEOPLE_ROLES R1
																INNER JOIN ROLE_RIGHTS R2 ON R1.ROLE_ID = R2.ROLE_ID
																INNER JOIN RIGHTS R3 ON R2.RIGHT_ID = R3.RIGHT_ID 
																WHERE R1.PERSON_ID = ''',AV_PERSON_ID,''' AND R3.RIGHT_NAME IN (''VIEW_ALL_AGREEMENT'',''MODIFY_ALL_AGREEMENT''))');
SET LS_FILTER_CONDITION5 = CONCAT(LS_FILTER_CONDITION5 ,' OR (EXISTS (SELECT S1.NEGOTIATION_LOCATION_ID FROM NEGOTIATION_LOCATION S1 
																WHERE S1.NEGOTIATION_ID = S0.NEGOTIATION_ID AND S1.ASSIGNEE_PERSON_ID = ''',AV_PERSON_ID,'''
																					))');	
																			
	IF AV_TYPE = 'A'   THEN 

        -- SET LS_FILTER_CONDITION = ' AND ';
		IF AV_AGREEMENT_REQUEST_ID IS NOT NULL AND AV_AGREEMENT_REQUEST_ID <> '' THEN 

		  SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' T1.AGREEMENT_REQUEST_ID LIKE  ''%',AV_AGREEMENT_REQUEST_ID,'%'' AND ');

		END IF;

        IF AV_KEYWORD IS NOT NULL  AND AV_KEYWORD <> '' THEN
             SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' KEYWORDS.KEYWORD LIKE ''%',AV_KEYWORD,'%'' AND ');
             SET JOIN_CONDITION = CONCAT(JOIN_CONDITION,' LEFT JOIN (
									    SELECT 
										AK.AGREEMENT_REQUEST_ID, 
                                        GROUP_CONCAT(IFNULL(SK.DESCRIPTION,'''')   SEPARATOR '','') AS KEYWORD
									FROM AGREEMENT_KEYWORDS AK
									LEFT JOIN SCIENCE_KEYWORD SK ON SK.SCIENCE_KEYWORD_CODE = AK.SCIENCE_KEYWORD_CODE
									GROUP BY AK.AGREEMENT_REQUEST_ID
								) KEYWORDS ON KEYWORDS.AGREEMENT_REQUEST_ID = T1.AGREEMENT_REQUEST_ID');
        END IF;

		IF AV_TITLE IS NOT NULL AND AV_TITLE <> '' THEN
		 SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,' LOWER(T1.TITLE ) LIKE  ''%',LOWER(AV_TITLE),'%'' AND ');
		
        END IF;

		IF AV_REQUESTOR_PERSON_ID IS NOT NULL AND AV_REQUESTOR_PERSON_ID <> ''  THEN

		 SET LS_FILTER_CONDITION =CONCAT( LS_FILTER_CONDITION,' LOWER(T1.REQUESTOR_PERSON_ID ) LIKE ''%',LOWER(AV_REQUESTOR_PERSON_ID),'%'' AND ');

		END IF;

	    IF AV_TYPE_CODE IS NOT NULL AND AV_TYPE_CODE <> ''  THEN

			SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.AGREEMENT_TYPE_CODE IN (',AV_TYPE_CODE,') AND ');

		END IF;

	    IF AV_STATUS_CODE IS NOT NULL AND AV_STATUS_CODE <> '' THEN

			SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.AGREEMENT_STATUS_CODE IN (',AV_STATUS_CODE,') AND ');

		END IF;

		IF AV_UNIT_NUMBER IS NOT NULL AND AV_UNIT_NUMBER <> '' THEN

			SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.UNIT_NUMBER IN (''',AV_UNIT_NUMBER,''') AND ');

		END IF;

		IF AV_ORGANIZATION IS NOT NULL AND AV_ORGANIZATION <> '' THEN

			SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' LOWER(SPO.ORGANIZATION) LIKE  ''%',LOWER(AV_ORGANIZATION),'%'' AND ');

		END IF;

		IF AV_PI_PERSON_ID IS NOT NULL AND AV_PI_PERSON_ID <> ''THEN

		 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' LOWER(PI.PERSON_ID)  LIKE ''%',LOWER(AV_PI_PERSON_ID),'%'' AND ');

		END IF;

        IF AV_REVIEW_STATUS IS NOT NULL  AND AV_REVIEW_STATUS <> '' THEN

			SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.REVIEW_STATUS_CODE IN (',AV_REVIEW_STATUS,') AND ');

		END IF;


		IF AV_ORGANIZATION_TYPE IS NOT NULL  AND AV_ORGANIZATION_TYPE <> '' THEN

			SET LS_FILTER_CONDITION_NEW = CONCAT(LS_FILTER_CONDITION_NEW,' AND T1.AGREEMENT_REQUEST_ID IN (SELECT AGREEMENT_REQUEST_ID FROM AGREEMENT_SPONSOR T21
																						 WHERE T21.SPONSOR_CODE IN (SELECT SP1.SPONSOR_CODE FROM SPONSOR SP1
																						WHERE SP1.SPONSOR_TYPE_CODE IN  (',AV_ORGANIZATION_TYPE,')))');

		END IF;
		IF AV_ADMIN_PERSON_ID IS NOT NULL AND AV_ADMIN_PERSON_ID <> '' THEN

			 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' LOWER(T1.ADMIN_PERSON_ID ) LIKE  ''%',LOWER(AV_ADMIN_PERSON_ID),'%'' AND ');

		END IF;

				IF LS_ADMIN_PERSON_IDS IS NOT NULL AND LS_ADMIN_PERSON_IDS <> '' THEN

			 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.ADMIN_PERSON_ID  IN (',LS_ADMIN_PERSON_IDS,') AND ');

		END IF;

		IF LS_IS_ADMIN_ASSIGNED IS NOT NULL AND LS_IS_ADMIN_ASSIGNED <> '' AND LS_IS_ADMIN_ASSIGNED = 'Y' THEN

			 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.ADMIN_PERSON_ID IS NOT NULL AND ');

		END IF;

		IF LS_IS_ADMIN_ASSIGNED IS NOT NULL AND LS_IS_ADMIN_ASSIGNED <> '' AND LS_IS_ADMIN_ASSIGNED = 'N' THEN

			 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.ADMIN_PERSON_ID IS NULL AND ');

		END IF;

		IF LS_CATEGORY IS NOT NULL AND LS_CATEGORY <> ''  THEN

			 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.CATEGORY_CODE  IN (',LS_CATEGORY,') AND ');

		END IF;

		IF LS_ASSIGNEE_IDS IS NOT NULL AND LS_ASSIGNEE_IDS <> ''  THEN
			 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' (EXISTS (SELECT S1.NEGOTIATION_LOCATION_ID FROM NEGOTIATION_LOCATION S1
 			WHERE S1.NEGOTIATION_ID = S0.NEGOTIATION_ID AND S1.ASSIGNEE_PERSON_ID IN (',LS_ASSIGNEE_IDS,'))) AND ');
		END IF;
		
		IF LS_LOCATION_STATUS IS NOT NULL AND LS_LOCATION_STATUS <> ''  THEN
			 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' (EXISTS (SELECT S1.NEGOTIATION_LOCATION_ID FROM NEGOTIATION_LOCATION S1
 			WHERE S1.NEGOTIATION_ID = S0.NEGOTIATION_ID AND S1.LOCATION_STATUS_CODE IN (',LS_LOCATION_STATUS,'))) AND ');
		END IF;

		IF LS_DESCEND_FLAG IS NOT NULL AND LS_DESCEND_FLAG <> '' AND LS_WIDGET_UNIT_NUMBER IS NOT NULL AND LS_WIDGET_UNIT_NUMBER <> '' THEN
			 SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' ((''',LS_WIDGET_UNIT_NUMBER,''' IS NOT NULL AND EXISTS (SELECT 1 FROM UNITS_BY_DESCEND_FLAG UD WHERE UD.UNIT_NUMBER = T1.UNIT_NUMBER )) OR ''',LS_WIDGET_UNIT_NUMBER,''' IS NULL ) AND ');
		END IF;

		IF (AV_START_DATE IS NOT NULL AND AV_START_DATE <> '') OR (AV_END_DATE IS NOT NULL AND AV_END_DATE <> '') THEN

			IF AV_START_DATE IS NOT NULL AND AV_END_DATE IS NOT NULL THEN

				SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.AGREEMENT_REQUEST_ID IN ( SELECT AGREEMENT_REQUEST_ID FROM AGREEMENT_ACTION_LOG WHERE ACTION_LOG_ID IN (select min(ACTION_LOG_ID) FROM AGREEMENT_ACTION_LOG WHERE  ACTION_TYPE_CODE IN (2,20) GROUP BY AGREEMENT_REQUEST_ID)

                                               AND TRIM(UPDATE_TIMESTAMP ) >= DATE_FORMAT(''',AV_START_DATE,''',''%Y-%m-%d'') AND DATE_FORMAT(UPDATE_TIMESTAMP,''%Y-%m-%d'') <=  DATE_FORMAT(''',AV_END_DATE,''',''%Y-%m-%d'') )AND ');

			END IF;

			IF AV_START_DATE IS NOT NULL AND AV_END_DATE IS NULL  THEN

				SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.AGREEMENT_REQUEST_ID IN ( SELECT AGREEMENT_REQUEST_ID FROM AGREEMENT_ACTION_LOG WHERE ACTION_LOG_ID IN (select min(ACTION_LOG_ID) FROM AGREEMENT_ACTION_LOG WHERE  ACTION_TYPE_CODE IN (2,20) GROUP BY AGREEMENT_REQUEST_ID)

                                               AND TRIM(UPDATE_TIMESTAMP ) >=   DATE_FORMAT(''',AV_START_DATE,''',''%Y-%m-%d'')) AND ');

			END IF;

			IF AV_END_DATE IS NOT NULL AND AV_START_DATE IS NULL THEN

				SET LS_FILTER_CONDITION = CONCAT( LS_FILTER_CONDITION,' T1.AGREEMENT_REQUEST_ID IN ( SELECT AGREEMENT_REQUEST_ID FROM AGREEMENT_ACTION_LOG WHERE ACTION_LOG_ID IN (select min(ACTION_LOG_ID) FROM AGREEMENT_ACTION_LOG WHERE  ACTION_TYPE_CODE IN (2,20) GROUP BY AGREEMENT_REQUEST_ID)

                                                  AND  DATE_FORMAT(UPDATE_TIMESTAMP,''%Y-%m-%d'')  <= DATE_FORMAT(''',AV_END_DATE,''',''%Y-%m-%d'')) AND ');

			END IF;

		END IF;

		IF AV_PROJECT_ID IS NOT NULL AND AV_PROJECT_ID <> '' THEN
		  SET LS_FILTER_CONDITION_NEW = CONCAT( LS_FILTER_CONDITION_NEW,' AND T1.AGREEMENT_REQUEST_ID IN (SELECT T54.AGREEMENT_ID FROM ASSOC_AGREEMENT T54
																						 WHERE T54.MODULE_ITEM_KEY LIKE ''%',AV_PROJECT_ID,'%'') ');
		END IF;
		IF LS_CUSTOM_DATA_CONDITION IS NOT NULL  AND LS_CUSTOM_DATA_CONDITION <> '' THEN
	 
			 SET LS_FILTER_CONDITION = CONCAT(LS_FILTER_CONDITION,'   EXISTS (SELECT 1 FROM AGREEMENT_CUSTOM_DATA_RT  WHERE AGREEMENT_REQUEST_ID = T1.AGREEMENT_REQUEST_ID',LS_CUSTOM_DATA_CONDITION,')');
			 SET LS_CUSTOM_DATA_JOIN = CONCAT(' LEFT JOIN AGREEMENT_CUSTOM_DATA_RT ACD
									 ON ACD.AGREEMENT_REQUEST_ID = T1.AGREEMENT_REQUEST_ID');
		    SET LS_CUSTOM_DATA_SEL	= LS_CUSTOM_DATA_SELECT;		
			  
		END IF;	
		IF LS_FILTER_CONDITION <> '' THEN		
			SET LS_FILTER_CONDITION = CONCAT(' AND ',LS_FILTER_CONDITION);
		END IF;
	
	END IF; -- end AV_TYPE = 'A'

   SELECT COUNT(1) 
	INTO IS_UNIT_ADMIN
	FROM
	PERSON_ROLES PR JOIN
	(SELECT ROLE_ID,RT.RIGHT_NAME
	   FROM ROLE_RIGHTS RR,
			RIGHTS RT
		WHERE RR.RIGHT_ID = RT.RIGHT_ID
		  AND RT.RIGHT_NAME IN ('AGREEMENT_ADMINISTRATOR')
	 )RLE ON RLE.ROLE_ID = PR.ROLE_ID
	WHERE PR.PERSON_ID = AV_PERSON_ID;

	SELECT 
		COUNT(1)
	INTO IS_GROUP_ADMIN 
	FROM
		ROLE_RIGHTS T1
	LEFT JOIN
		RIGHTS T2 ON T1.RIGHT_ID = T2.RIGHT_ID
	LEFT JOIN
		PERSON_ROLES T3 ON T1.ROLE_ID = T3.ROLE_ID
	WHERE T3.PERSON_ID = AV_PERSON_ID
	  AND T2.RIGHT_NAME = 'VIEW_ADMIN_GROUP_AGREEMENT';

	SELECT  COUNT(1) INTO IS_DEPT_ADMIN
	FROM PERSON_ROLES PR JOIN
	(SELECT ROLE_ID,RT.RIGHT_NAME
	   FROM ROLE_RIGHTS RR,
			RIGHTS RT
		WHERE RR.RIGHT_ID = RT.RIGHT_ID
		  AND RT.RIGHT_NAME IN ('VIEW_ALL_AGREEMENT' , 'MODIFY_ALL_AGREEMENT')
	 )RLE ON RLE.ROLE_ID = PR.ROLE_ID
	WHERE PR.PERSON_ID = AV_PERSON_ID ;

	IF AV_TAB_TYPE = 'IN_PROGRESS_AGREEMENTS' THEN

		IF IS_UNIT_ADMIN > 0 THEN
			SET LS_TAB_QUERY = CONCAT(LS_TAB_QUERY,' ',LS_FILTER_CONDITION2  , LS_FILTER_CONDITION5,') AND T1.AGREEMENT_STATUS_CODE = 1 ');
		ELSEIF IS_GROUP_ADMIN > 0 THEN
			SET LS_TAB_QUERY = CONCAT(LS_TAB_QUERY ,' ',LS_FILTER_CONDITION4, LS_FILTER_CONDITION5,')) AND T1.AGREEMENT_STATUS_CODE = 1 ');
		ELSE
			SET LS_TAB_QUERY = CONCAT(LS_TAB_QUERY,' ',LS_FILTER_CONDITION3 , LS_FILTER_CONDITION5,') AND T1.AGREEMENT_STATUS_CODE = 1 ');
		END IF;

	ELSEIF AV_TAB_TYPE = 'ALL_AGREEMENTS' THEN

		IF IS_UNIT_ADMIN > 0 THEN
			SET LS_TAB_QUERY = CONCAT(LS_TAB_QUERY,' ',LS_FILTER_CONDITION2 , LS_FILTER_CONDITION5,')');
		ELSEIF IS_GROUP_ADMIN > 0 THEN
			SET LS_TAB_QUERY = CONCAT(LS_TAB_QUERY,' ',LS_FILTER_CONDITION4 , LS_FILTER_CONDITION5,'))');
		ELSE
			SET LS_TAB_QUERY = CONCAT(LS_TAB_QUERY,' ',LS_FILTER_CONDITION3 , LS_FILTER_CONDITION5,')' );
		END IF;

	ELSEIF AV_TAB_TYPE = 'NEW_SUBMISSIONS' THEN
		IF IS_UNIT_ADMIN > 0 THEN
