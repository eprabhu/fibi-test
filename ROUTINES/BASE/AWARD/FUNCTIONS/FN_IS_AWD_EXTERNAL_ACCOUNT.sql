-- `FN_IS_AWD_EXTERNAL_ACCOUNT`; 


CREATE FUNCTION `FN_IS_AWD_EXTERNAL_ACCOUNT`(
AV_AWARD_ID	VARCHAR(22)
) RETURNS varchar(6) 
    DETERMINISTIC
BEGIN
DECLARE LI_COUNT INT(3);
	SELECT COUNT(*) INTO LI_COUNT 
	FROM AWARD 
	WHERE AWARD_ID = AV_AWARD_ID
	AND ACCOUNT_TYPE_CODE IN (1,2,4,6);
	IF LI_COUNT = 0 THEN  
		RETURN 'FALSE';
	ELSE
		RETURN 'TRUE';
	END IF;
END
