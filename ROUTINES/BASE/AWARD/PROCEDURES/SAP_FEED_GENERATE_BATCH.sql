							IF LS_PROCESS <> 'TRAN' THEN
							
																	
									SELECT IFNULL(MAX(LINE_ITEM),0)+1 INTO LS_CUR_LINE_ITEM FROM SAP_FEED_TMPL_FM_BUDGET;
																
									SELECT LPAD(LS_CUR_LINE_ITEM,6,'0') INTO LS_LINE_ITEM FROM DUAL;

								
