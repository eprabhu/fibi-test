							LS_LINE_ITEM_DESCRIPTION,
							LS_CREATE_STATUS,
							LS_UPDATE_USER,
							UTC_TIMESTAMP(),
							'F'
							);
							
						END IF;
						SET LS_PROCESS = NULL;
						SET LS_COMMITMENT_ITEM = NULL;
						SET LS_IO_CODE = NULL;
						SET LS_LINE_ITEM_DESCRIPTION = NULL;
						SET LS_CREATE_STATUS = NULL;
						SET LS_BUDGET_CATEGORY_CODE = NULL;
