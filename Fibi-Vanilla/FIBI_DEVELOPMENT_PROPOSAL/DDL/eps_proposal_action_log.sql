DROP TABLE IF EXISTS `eps_proposal_action_log`;

CREATE TABLE `eps_proposal_action_log` (
  `ACTION_LOG_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ACTION_TYPE_CODE` varchar(3) DEFAULT NULL,
  `MESSAGE` varchar(1000) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `PROPOSAL_ID` int(11) DEFAULT NULL,
  `EPS_PROPOSAL_COMMENT_ID` int(11) DEFAULT NULL,
  PRIMARY KEY (`ACTION_LOG_ID`),
  KEY `EPS_PROPOSAL_ACTION_LOG_FK1` (`ACTION_TYPE_CODE`),
  KEY `EPS_PROPOSAL_ACTION_LOG_FK2` (`PROPOSAL_ID`),
  KEY `EPS_PROPOSAL_ACTION_LOG_FK3` (`EPS_PROPOSAL_COMMENT_ID`),
  CONSTRAINT `EPS_PROPOSAL_ACTION_LOG_FK1` FOREIGN KEY (`ACTION_TYPE_CODE`) REFERENCES `eps_proposal_action_type` (`ACTION_TYPE_CODE`),
  CONSTRAINT `EPS_PROPOSAL_ACTION_LOG_FK2` FOREIGN KEY (`PROPOSAL_ID`) REFERENCES `eps_proposal` (`PROPOSAL_ID`),
  CONSTRAINT `EPS_PROPOSAL_ACTION_LOG_FK3` FOREIGN KEY (`EPS_PROPOSAL_COMMENT_ID`) REFERENCES `eps_proposal_comments` (`EPS_PROPOSAL_COMMENT_ID`)
) ;
