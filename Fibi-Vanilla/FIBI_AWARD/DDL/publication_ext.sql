DROP TABLE IF EXISTS `publication_ext`;

CREATE TABLE `publication_ext` (
  `RESEARCHER_PUBLICATION_EXT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PUBLICATION_TYPE_ID` int(11) DEFAULT NULL,
  `PERSON_ID` varchar(40) NOT NULL,
  `PUBLICATION_TYPE_CODE` varchar(3) DEFAULT NULL,
  `CO_AUTHORSHIP` text,
  `QUICK_URL` varchar(400) DEFAULT NULL,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `STATUS` varchar(1) DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_PUBLICATION_EXT_ID`),
  KEY `RESEARCHER_PUBLICATION_EXT_FK1` (`PUBLICATION_TYPE_CODE`),
  CONSTRAINT `RESEARCHER_PUBLICATION_EXT_FK1` FOREIGN KEY (`PUBLICATION_TYPE_CODE`) REFERENCES `publication_type` (`PUBLICATION_TYPE_CODE`)
) ;
