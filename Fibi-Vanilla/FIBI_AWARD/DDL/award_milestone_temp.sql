DROP TABLE IF EXISTS `award_milestone_temp`;

CREATE TABLE `award_milestone_temp` (
  `TEMP_ID` int(11) NOT NULL AUTO_INCREMENT,
  `AWARD_NUMBER` varchar(255) DEFAULT NULL,
  `MILESTONE` varchar(1000) DEFAULT NULL,
  `SEQUENCE_NUMBER` int(11) DEFAULT NULL,
  `OLD_MILESTONE_NUMBER` varchar(30) DEFAULT NULL,
  `NEW_MILESTONE_NUMBER` varchar(30) DEFAULT NULL,
  PRIMARY KEY (`TEMP_ID`)
) ;
