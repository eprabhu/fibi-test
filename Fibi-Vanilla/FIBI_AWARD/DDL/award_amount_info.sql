DROP TABLE IF EXISTS `award_amount_info`;

CREATE TABLE `award_amount_info` (
  `AWARD_AMOUNT_INFO_ID` int(11) NOT NULL AUTO_INCREMENT,
  `AWARD_ID` int(11) DEFAULT NULL,
  `AWARD_NUMBER` varchar(12) DEFAULT NULL,
  `SEQUENCE_NUMBER` int(11) DEFAULT NULL,
  `TRANSACTION_ID` decimal(22,0) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `ANTICIPATED_CHANGE` decimal(15,2) DEFAULT NULL,
  `ANTICIPATED_CHANGE_DIRECT` decimal(15,2) DEFAULT NULL,
  `ANTICIPATED_CHANGE_INDIRECT` decimal(15,2) DEFAULT NULL,
  `OBLIGATED_CHANGE` decimal(15,2) DEFAULT NULL,
  `OBLIGATED_CHANGE_DIRECT` decimal(15,2) DEFAULT NULL,
  `OBLIGATED_CHANGE_INDIRECT` decimal(15,2) DEFAULT NULL,
  `ANTICIPATED_TOTAL_DIRECT` decimal(15,2) DEFAULT NULL,
  `ANTICIPATED_TOTAL_INDIRECT` decimal(15,2) DEFAULT NULL,
  `ANTICIPATED_TOTAL_AMOUNT` decimal(15,2) DEFAULT NULL,
  `OBLIGATED_TOTAL_DIRECT` decimal(15,2) DEFAULT NULL,
  `OBLIGATED_TOTAL_INDIRECT` decimal(15,2) DEFAULT NULL,
  `AMOUNT_OBLIGATED_TO_DATE` decimal(15,2) DEFAULT NULL,
  `ANT_DISTRIBUTABLE_AMOUNT` decimal(15,2) DEFAULT NULL,
  `OBLI_DISTRIBUTABLE_AMOUNT` decimal(15,2) DEFAULT NULL,
  `FINAL_EXPIRATION_DATE` datetime DEFAULT NULL,
  `CURRENT_FUND_EFFECTIVE_DATE` datetime DEFAULT NULL,
  `OBLIGATION_EXPIRATION_DATE` datetime DEFAULT NULL,
  `TOTAL_COST_IN_CURRENCY` decimal(15,2) DEFAULT NULL,
  `CURRENCY_CODE` varchar(3) DEFAULT NULL,
  PRIMARY KEY (`AWARD_AMOUNT_INFO_ID`),
  KEY `AWARD_AMOUNT_INFO_FK1` (`AWARD_ID`),
  KEY `AWARD_AMOUNT_INFO_FK2` (`TRANSACTION_ID`),
  KEY `AWARD_AMOUNT_INFO_FK3` (`CURRENCY_CODE`),
  CONSTRAINT `AWARD_AMOUNT_INFO_FK1` FOREIGN KEY (`AWARD_ID`) REFERENCES `award` (`AWARD_ID`),
  CONSTRAINT `AWARD_AMOUNT_INFO_FK2` FOREIGN KEY (`TRANSACTION_ID`) REFERENCES `award_amount_transaction` (`TRANSACTION_ID`),
  CONSTRAINT `AWARD_AMOUNT_INFO_FK3` FOREIGN KEY (`CURRENCY_CODE`) REFERENCES `currency` (`CURRENCY_CODE`)
) ;
