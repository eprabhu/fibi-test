DROP TABLE IF EXISTS `loa_template_identifier`;

CREATE TABLE `loa_template_identifier` (
  `LOA_TEMPLATE_IDENTIFIER_ID` int NOT NULL AUTO_INCREMENT,
  `TYPE_CODE` int DEFAULT NULL,
  `KEY_WORD` varchar(50) DEFAULT NULL,
  `EVAL_VALUE` varchar(50) DEFAULT NULL,
  `EVAL_CONDITION` varchar(20) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  PRIMARY KEY (`LOA_TEMPLATE_IDENTIFIER_ID`),
  KEY `LOA_TEMPLATE_FK_1_idx` (`TYPE_CODE`),
  CONSTRAINT `LOA_TEMPLATE_FK_1` FOREIGN KEY (`TYPE_CODE`) REFERENCES `loa_template_type` (`TYPE_CODE`)
) ;
