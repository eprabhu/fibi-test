
DROP TABLE IF EXISTS `envelope_document`;

CREATE TABLE `envelope_document` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENVELOPE_ID` varchar(255) DEFAULT NULL,
  `DOCUMENT_ID` varchar(255) DEFAULT NULL,
  `DOCUMENT_ID_GUID` varchar(255) DEFAULT NULL,
  `DOCUMENT_NAME` varchar(255) DEFAULT NULL,
  `DOCUMENT_ORDER` varchar(255) DEFAULT NULL,
  `TYPE` varchar(255) DEFAULT NULL,
  `DATA` longblob,
  `FILE_DATA_ID` varchar(255) DEFAULT NULL,
  `FILE_PATH` varchar(255) DEFAULT NULL,
  `ORIGINAL_FILE_NAME` varchar(255) DEFAULT NULL,
  `IS_ARCHIVED` varchar(45) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime(6) DEFAULT NULL,
  `UPDATE_USER` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`ID`)
) ;
