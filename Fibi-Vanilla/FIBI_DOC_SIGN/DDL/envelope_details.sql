
DROP TABLE IF EXISTS `envelope_details`;

CREATE TABLE `envelope_details` (
  `ENVELOPE_DETAILS_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENVELOPE_ID` varchar(255) NOT NULL,
  `AGREEMENT_ESIGN_ID` int(11) NOT NULL,
  `MODULE_CODE` varchar(255) DEFAULT NULL,
  `MODULE_ITEM_KEY` varchar(255) DEFAULT NULL,
  `SUB_MODULE_CODE` varchar(255) DEFAULT NULL,
  `SUB_MODULE_ITEM_KEY` varchar(255) DEFAULT NULL,
  `STATUS` varchar(255) DEFAULT NULL,
  `EMAIL_SUBJECT` varchar(1000) DEFAULT NULL,
  `EMAIL_BLURB` longtext,
  `CREATED_DATE_TIME` varchar(255) DEFAULT NULL,
  `LAST_MODIFIED_DATE_TIME` varchar(255) DEFAULT NULL,
  `DELIVERED_DATE_TIME` varchar(255) DEFAULT NULL,
  `INITIAL_SENT_DATE_TIME` varchar(255) DEFAULT NULL,
  `COMPLETED_DATE_TIME` varchar(255) DEFAULT NULL,
  `EXPIRE_DATE_TIME` varchar(255) DEFAULT NULL,
  `EXPIRE_AFTER` varchar(255) DEFAULT NULL,
  `URL` varchar(4000) DEFAULT NULL,
  `IS_COMPLETE` varchar(1) DEFAULT NULL,
