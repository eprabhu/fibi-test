
DROP TABLE IF EXISTS `form_builder_usage`;

CREATE TABLE `form_builder_usage` (
  `FORM_USAGE_ID` int(11) NOT NULL AUTO_INCREMENT,
  `FORM_BUILDER_ID` int(11) NOT NULL,
  `FORM_BUILDER_NUMBER` varchar(20) NOT NULL,
  `FORM_ORDER_NUMBER` int(11) NOT NULL,
  `MODULE_CODE` varchar(3) NOT NULL,
  `SUB_MODULE_CODE` varchar(3) NOT NULL,
  `BUSINESS_RULE_ID` int(11) DEFAULT NULL,
  `DESCRIPTION` varchar(300) NOT NULL,
  `IS_ACTIVE` varchar(1) NOT NULL,
  `CREATE_TIMESTAMP` datetime NOT NULL,
  `CREATE_USER` varchar(60) NOT NULL,
  `UPDATE_TIMESTAMP` datetime NOT NULL,
  `UPDATE_USER` varchar(60) NOT NULL,
  PRIMARY KEY (`FORM_USAGE_ID`),
  KEY `FORM_BUILDER_USAGE_FK1` (`FORM_BUILDER_ID`),
  CONSTRAINT `FORM_BUILDER_USAGE_FK1` FOREIGN KEY (`FORM_BUILDER_ID`) REFERENCES `form_builder_header` (`FORM_BUILDER_ID`)
) ;
