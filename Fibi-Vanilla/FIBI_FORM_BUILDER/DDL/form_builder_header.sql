DROP TABLE IF EXISTS `FORM_BUILDER_HEADER`;

CREATE TABLE `form_builder_header` (
  `FORM_BUILDER_ID` int NOT NULL AUTO_INCREMENT,
  `FORM_BUILDER_NUMBER` varchar(20) DEFAULT NULL,
  `VERSION_NUMBER` int DEFAULT NULL,
  `VERSION_STATUS` varchar(10) DEFAULT NULL,
  `MODULE_CODE` varchar(3) DEFAULT NULL,
  `SUB_MODULE_CODE` varchar(3) DEFAULT NULL,
  `BUSINESS_RULE_ID` int DEFAULT NULL,
  `DESCRIPTION` varchar(4000) DEFAULT NULL,
  `CREATE_TIMESTAMP` datetime DEFAULT NULL,
  `CREATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `IS_ACTIVE` varchar(1) DEFAULT NULL,
  `TITLE` varchar(1000) DEFAULT NULL,
  `VERSION_MODE` varchar(1) NOT NULL DEFAULT 'A',
  `PUBLISHED_DATE` datetime DEFAULT NULL,
  PRIMARY KEY (`FORM_BUILDER_ID`)
) ;
