
DROP TABLE IF EXISTS `form_builder_section`;

CREATE TABLE `form_builder_section` (
  `FORM_BUILDER_SECTION_ID` int(11) NOT NULL AUTO_INCREMENT,
  `FORM_BUILDER_ID` int(11) NOT NULL,
  `FORM_BUILDER_NUMBER` varchar(20) DEFAULT NULL,
  `SECTION_NAME` varchar(200) DEFAULT NULL,
  `SECTION_ORDER_NUMBER` int(11) DEFAULT NULL,
  `MODULE_CODE` varchar(3) DEFAULT NULL,
  `SUB_MODULE_CODE` varchar(3) DEFAULT NULL,
  `BUSINESS_RULE_ID` int(11) DEFAULT NULL,
  `DESCRIPTION` varchar(300) NOT NULL,
  `HELP_TEXT` varchar(1000) DEFAULT NULL,
  `HEADER_INSTRUCTION` varchar(4000) DEFAULT NULL,
  `FOOTER_INSTRUCTION` varchar(4000) DEFAULT NULL,
  `IS_ACTIVE` varchar(1) NOT NULL,
  `UPDATE_TIMESTAMP` datetime NOT NULL,
  `UPDATE_USER` varchar(60) NOT NULL,
  PRIMARY KEY (`FORM_BUILDER_SECTION_ID`),
  KEY `FORM_BUILDER_SECTION_FK1` (`FORM_BUILDER_ID`),
  CONSTRAINT `FORM_BUILDER_SECTION_FK1` FOREIGN KEY (`FORM_BUILDER_ID`) REFERENCES `form_builder_header` (`FORM_BUILDER_ID`)
) ;