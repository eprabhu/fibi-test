CREATE TRIGGER RESTRICT_INSERT_INTO_PARAMETER
BEFORE INSERT 
ON PARAMETER
FOR EACH ROW
BEGIN
    IF NEW.PARAMETER_NAME = 'EMAIL_NOTIFICATION_TEST_ENABLED' AND NEW.VALUE = 'N' THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Inserting the VALUE column to "N" is not allowed for PARAMETER_NAME = \'EMAIL_NOTIFICATION_TEST_ENABLED\'.';
    END IF;
END