CREATE TRIGGER RESTRICT_DELETE_FROM_PARAMETER
BEFORE DELETE 
ON PARAMETER
FOR EACH ROW
BEGIN
    IF OLD.PARAMETER_NAME = 'EMAIL_NOTIFICATION_TEST_ENABLED' THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Deleting the row with PARAMETER_NAME = \'EMAIL_NOTIFICATION_TEST_ENABLED\' is not allowed.';
    END IF;
END
