				IFNULL(T6.IS_MANUALLY_ADDED, 'N'),
				T6.PROJECT_TITLE,
				T6.GRANT_CALL_NAME,
				T6.PROP_PERSON_ROLE_ID,
				T6.AMOUNT,
				T6.END_DATE,
				T6.START_DATE,
