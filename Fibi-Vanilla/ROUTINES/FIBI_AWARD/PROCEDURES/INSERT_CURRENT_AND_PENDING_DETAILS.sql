											LI_CP_REPORT_HEADER_ID_NEXT_VAL,
											2,
											LI_LINKED_MODULE_ITEM_ID,
											LS_EXCLUDED_FLAG,
										    LS_MANUALLY_ADDED_FLAG,
											AV_UPDATE_USER,
											UTC_TIMESTAMP(),
											LS_PROJECT_TITLE,
											LS_GRANT_CALL_NAME,
											LI_PROP_PERSON_ROLE_ID,
