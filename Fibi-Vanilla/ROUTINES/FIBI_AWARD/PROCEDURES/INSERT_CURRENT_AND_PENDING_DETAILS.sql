											LS_START_DATE,
											LS_SPONSOR_TYPE_CODE,
											LI_FUNDING_STATUS_CODE,
											LS_SPONSOR_CODE,
											LS_CURRENCY_CODE,
											LI_PERCENT_OF_EFFORT
