			SELECT COUNT(1) INTO LI_AWD_BUDGT_HEADER_CNT FROM AWARD_BUDGET_HEADER
			WHERE AWARD_ID = AV_AWARD_ID;
			
			SELECT COUNT(1) INTO LI_AWARD_CONTACT_CNT
			FROM AWARD_CONTACT
			WHERE AWARD_ID = AV_AWARD_ID;
			
			SELECT COUNT(1) INTO LI_TOTAL_COST_SHARE_CNT
			FROM AWARD_COST_SHARE 
			WHERE AWARD_ID = AV_AWARD_ID;
			
