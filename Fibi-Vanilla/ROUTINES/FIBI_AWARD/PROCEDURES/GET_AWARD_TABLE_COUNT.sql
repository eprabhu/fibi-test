CREATE PROCEDURE `GET_AWARD_TABLE_COUNT`(
AV_TYPE					VARCHAR(20)
)
BEGIN

DECLARE LS_ERROR_MSG		VARCHAR(1000);
DECLARE LS_TABLE_NAME		VARCHAR(30);
DECLARE LI_FLAG				INT;

DECLARE AV_AWARD_ID			INT;
DECLARE LI_AWARD_ID			INT;
DECLARE LS_AWARD_NUMBER		VARCHAR(12);
DECLARE LI_SEQUENCE_NUMBER	INT;
DECLARE LI_SCIENCE_KEYWORD_CNT	INT(10);
DECLARE LI_AWARD_RSRCH_CNT		INT(10);
DECLARE LI_AWARD_KPI_CNT		INT(10);
DECLARE LI_AWARD_KPI_CRIT_CNT	INT(10);
DECLARE	LI_AWD_BUDGT_CAL_AMT_CNT 			INT(10);	
DECLARE LI_AWD_BUDGT_NON_PERSN_DTL_CNT		INT(10);
DECLARE LI_AWD_BUDGT_PERSN_DTL_CNT			INT(10);
DECLARE LI_AWD_BUDGT_RATE_BASE_CNT			INT(10);
DECLARE LI_AWD_BUDGT_DETL_ID_CNT			INT(10);
DECLARE LI_AWD_BUDGT_PERIOD_CNT				INT(10);
DECLARE LI_AWAD_RATE_ID_CNT					INT(10);
DECLARE	LI_AWD_BUDGT_HEADER_CNT				INT(10);
DECLARE LI_AWARD_CONTACT_CNT				INT(10);
DECLARE LI_TOTAL_COST_SHARE_CNT				INT(10);
DECLARE LI_QST_ANS_HDR_CNT					INT(10);
DECLARE	LI_QST_ANSWER_CNT					INT(10);
DECLARE	LI_QST_ANS_ATT_CNT					INT(10);
DECLARE LI_TOTAL_AWARD_FUNDING_PROP_CNT		INT(10);
DECLARE LI_AWARD_PERSON_UNIT_CNT			INT(10);
DECLARE LI_AWARD_PERSON_ATTACHMNT_CNT		INT(10);
DECLARE	LI_AWARD_PERSONS_CNT				INT(10);
DECLARE LI_AWARD_MANPWR_CNT					INT(10);
DECLARE	LI_AWD_MANPW_RES_CNT				INT(10);
DECLARE	LI_AWARD_MILE_ST_CNT				INT(10);
DECLARE	LI_TOTAL_AWARD_CUSTOM_CNT			INT(10);
DECLARE LI_TOTAL_AWARD_PUBLICATION_CNT		INT(10);
DECLARE LI_TOTAL_AWARD_ACHIEVMNT_ATTACH_CNT	INT(10);
DECLARE LI_AWAD_ASSOC_CNT					INT(10);
DECLARE LI_AWAD_ASSOC_DTL_CNT				INT(10);
DECLARE	LI_PROJECT_TEAM_CNT					INT(10);
DECLARE LI_QST_AWD_ANS_HDR_CNT 				INT(10);
DECLARE LI_QST_AWD_ANSWER_CNT 				INT(10);
DECLARE LI_QST_AWD_ANS_ATT_CNT 				INT(10);
DECLARE LI_AWARD_REPORT_TERMS_CNT			INT(10);
DECLARE LI_AWARD_REPT_TERM_RECPT_CNT		INT(10);
DECLARE LI_AWD_REPT_TRACK_CNT				INT(10);
DECLARE LI_AWARD_RPT_TRCK_FILE_CNT			INT(10);
DECLARE LI_AWARD_PERSON_ROLE_ID_CNT			INT(10);
DECLARE LI_AWARD_APPR_EQP_CNT			INT(10);
DECLARE LI_AWARD_APRVD_FOREIGN_TRA_CNT	INT(10);
DECLARE LI_AWARD_SPECIAL_REVIEW_CNT		INT(10);
DECLARE LI_AWARD_APPROVED_SUBAWARDS_CNT		INT(10);
DECLARE LI_SPONSOR_TERM_CNT					INT(10);


