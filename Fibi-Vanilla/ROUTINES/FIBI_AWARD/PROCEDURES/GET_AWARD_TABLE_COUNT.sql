												 FROM QUEST_ANSWER_HEADER
												 WHERE MODULE_ITEM_CODE = 1
												 AND MODULE_SUB_ITEM_CODE = 0
												 AND MODULE_ITEM_KEY = AV_AWARD_ID
												 );
												 
			SELECT COUNT(1) INTO LI_QST_AWD_ANS_ATT_CNT FROM QUEST_ANSWER_ATTACHMENT
			WHERE QUESTIONNAIRE_ANSWER_ID IN(SELECT QUESTIONNAIRE_ANSWER_ID FROM QUEST_ANSWER
											 WHERE QUESTIONNAIRE_ANS_HEADER_ID IN(SELECT QUESTIONNAIRE_ANS_HEADER_ID
																				 FROM QUEST_ANSWER_HEADER
																				 WHERE MODULE_ITEM_CODE = 1
																				 AND MODULE_SUB_ITEM_CODE = 0
																				 AND MODULE_ITEM_KEY = AV_AWARD_ID
																				)
											);
											
			SELECT COUNT(1) INTO LI_AWARD_REPORT_TERMS_CNT
			FROM AWARD_REPORT_TERMS
			WHERE AWARD_ID = AV_AWARD_ID;

			SELECT COUNT(1) INTO LI_AWARD_REPT_TERM_RECPT_CNT
			FROM AWARD_REPORT_TERM_RECIPIENT
			WHERE AWARD_ID = AV_AWARD_ID;

			SELECT COUNT(1) INTO LI_AWD_REPT_TRACK_CNT
			FROM AWARD_REPORT_TRACKING
			WHERE AWARD_ID = AV_AWARD_ID;

			SELECT COUNT(1) INTO LI_AWARD_RPT_TRCK_FILE_CNT
			FROM AWARD_REPORT_TRACKING_FILE
			WHERE AWARD_ID = AV_AWARD_ID;
			
			SELECT COUNT(1) INTO LI_AWARD_PERSON_ROLE_ID_CNT
			FROM AWARD_PERSON_ROLES
			WHERE AWARD_ID = AV_AWARD_ID;
			
			SELECT COUNT(1) INTO LI_AWARD_APPR_EQP_CNT
			FROM AWARD_APPROVED_EQUIPMENT 
			WHERE AWARD_ID = AV_AWARD_ID;
			
			SELECT COUNT(1) INTO LI_AWARD_APRVD_FOREIGN_TRA_CNT
			FROM AWARD_APPROVED_FOREIGN_TRAVEL 
			WHERE AWARD_ID = AV_AWARD_ID;
			
			SELECT COUNT(1) INTO LI_AWARD_SPECIAL_REVIEW_CNT
			FROM AWARD_SPECIAL_REVIEW 
			WHERE AWARD_ID = AV_AWARD_ID;
			
			SELECT COUNT(1) INTO LI_AWARD_APPROVED_SUBAWARDS_CNT
			FROM AWARD_APPROVED_SUBAWARDS 
			WHERE AWARD_ID = AV_AWARD_ID;
			
			SELECT COUNT(1) INTO LI_SPONSOR_TERM_CNT
			FROM AWARD_SPONSOR_TERM
			WHERE AWARD_ID = AV_AWARD_ID;
				
			IF AV_TYPE = 'BEFORE' THEN
				-- -------------------STARTS INSERTION----------------------------------------
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_SCIENCE_KEYWORD',LI_SCIENCE_KEYWORD_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_RESEARCH_AREAS',LI_AWARD_RSRCH_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_KPI',LI_AWARD_KPI_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_KPI_CRITERIA',LI_AWARD_KPI_CRIT_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_BUDGET_DET_CAL_AMT',LI_AWD_BUDGT_CAL_AMT_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_BUDGET_NON_PERSON_DETAIL',LI_AWD_BUDGT_NON_PERSN_DTL_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_BUDGET_PERSON_DETAIL',LI_AWD_BUDGT_PERSN_DTL_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_BUDGET_RATE_AND_BASE',LI_AWD_BUDGT_RATE_BASE_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
				VALUES(AV_AWARD_ID,LS_AWARD_NUMBER,LI_SEQUENCE_NUMBER,'AWARD_BUDGET_DETAIL',LI_AWD_BUDGT_DETL_ID_CNT,NOW(),'admin');
				
				INSERT INTO AWARD_TABLE_TOTAL_COUNT(AWARD_ID,AWARD_NUMBER,SEQUENCE_NUMBER,TABLE_NAME, BEFORE_TOTAL_COUNT, UPDATETIMESTAMP, UPDATE_USER)
