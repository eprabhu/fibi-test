


CREATE PROCEDURE `COI_DELETE_SFI_BY_ID`(
AV_PERSON_ENTITY_ID VARCHAR(60)
)
BEGIN

	SET FOREIGN_KEY_CHECKS=0; 
	SET SQL_SAFE_UPDATES = 0;
	
    DELETE FROM PERSON_ENTITY_ACTION_LOG
	WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID;
    
    DELETE FROM PERSON_ENTITY_RELATIONSHIP
	WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID;
    
     DELETE FROM COI_QUEST_ANSWER_ATTACHMENT 
    WHERE QUESTIONNAIRE_ANSWER_ID IN (select QUESTIONNAIRE_ANSWER_ID from COI_QUEST_ANSWER 
									WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
																			WHERE MODULE_ITEM_CODE = 8 AND MODULE_SUB_ITEM_CODE=801 AND MODULE_ITEM_KEY = AV_PERSON_ENTITY_ID));
	DELETE FROM COI_QUEST_TABLE_ANSWER 
    WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
											WHERE MODULE_ITEM_CODE = 8 AND MODULE_SUB_ITEM_CODE=801 AND MODULE_ITEM_KEY = AV_PERSON_ENTITY_ID);
												
	DELETE FROM COI_QUEST_ANSWER 
    WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
											WHERE MODULE_ITEM_CODE = 8 AND MODULE_SUB_ITEM_CODE=801 AND MODULE_ITEM_KEY = AV_PERSON_ENTITY_ID);
    
    DELETE FROM DISCLOSURE_ACTION_LOG WHERE DISCLOSURE_ID IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_CONFLICT_HISTORY 
    WHERE DISCLOSURE_ID IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_CONFLICT_HISTORY 
    WHERE DISCLOSURE_ID IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_REVIEW_COMMENT_TAGS 
    WHERE COI_REVIEW_ID IN (SELECT COI_REVIEW_ID FROM COI_REVIEW WHERE DISCLOSURE_ID IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));
    
    DELETE FROM COI_REVIEW_ASSIGNEE_HISTORY 
    WHERE COI_REVIEW_ID IN (SELECT COI_REVIEW_ID FROM COI_REVIEW WHERE DISCLOSURE_ID IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));
    
    DELETE FROM COI_REVIEW_COMMENT_ATTACHMENT 
    WHERE COI_REVIEW_ID IN (SELECT COI_REVIEW_ID FROM COI_REVIEW WHERE DISCLOSURE_ID IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));

	DELETE FROM COI_REVIEW 
    WHERE DISCLOSURE_ID IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_QUEST_ANSWER_ATTACHMENT 
    WHERE QUESTIONNAIRE_ANSWER_ID IN (select QUESTIONNAIRE_ANSWER_ID from COI_QUEST_ANSWER 
									WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
																			WHERE MODULE_ITEM_CODE = 8 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID)));
	DELETE FROM COI_QUEST_TABLE_ANSWER 
    WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
											WHERE MODULE_ITEM_CODE = 8 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));
												
	DELETE FROM COI_QUEST_ANSWER 
    WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
											WHERE MODULE_ITEM_CODE = 8 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));
    
    DELETE FROM DISCL_COMMENT WHERE MODULE_CODE=8 AND MODULE_ITEM_KEY IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_DISCLOSURE WHERE DISCLOSURE_ID IN (SELECT DISCLOSURE_ID FROM COI_DISCL_ENT_PROJ_DETAILS WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_TRAVEL_DISCLOSURE_TRAVELER 
    WHERE TRAVEL_DISCLOSURE_ID IN (SELECT TRAVEL_DISCLOSURE_ID FROM COI_TRAVEL_DISCLOSURE WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM TRAVEL_DISCLOSURE_ACTION_LOG 
    WHERE TRAVEL_DISCLOSURE_ID IN (SELECT TRAVEL_DISCLOSURE_ID FROM COI_TRAVEL_DISCLOSURE WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_TRAVEL_CONFLICT_HISTORY 
    WHERE TRAVEL_DISCLOSURE_ID IN (SELECT TRAVEL_DISCLOSURE_ID FROM COI_TRAVEL_DISCLOSURE WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_QUEST_ANSWER_ATTACHMENT 
    WHERE QUESTIONNAIRE_ANSWER_ID IN (select QUESTIONNAIRE_ANSWER_ID from COI_QUEST_ANSWER 
									WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
																			WHERE MODULE_ITEM_CODE = 24 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT TRAVEL_DISCLOSURE_ID FROM COI_TRAVEL_DISCLOSURE 
																																							WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID)));
	DELETE FROM COI_QUEST_TABLE_ANSWER 
    WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
											WHERE MODULE_ITEM_CODE = 24 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT TRAVEL_DISCLOSURE_ID FROM COI_TRAVEL_DISCLOSURE 
																															WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));
												
	DELETE FROM COI_QUEST_ANSWER 
    WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
											WHERE MODULE_ITEM_CODE = 24 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT TRAVEL_DISCLOSURE_ID FROM COI_TRAVEL_DISCLOSURE 
																															WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));
    
    DELETE FROM COI_TRAVEL_DISCLOSURE WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID;
    
    DELETE FROM COI_DISCL_ENT_PROJ_DETAILS 
    WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID;
    
    DELETE FROM PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID;
    
    
    DELETE FROM OPA_ACTION_LOG 
    WHERE OPA_DISCLOSURE_ID IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM OPA_REVIEW 
    WHERE OPA_DISCLOSURE_ID IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM OPA_FORM_BUILDER_DETAILS 
    WHERE OPA_DISCLOSURE_ID IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM OPA_INSTITUTE_RESOURCE_USE 
    WHERE OPA_DISCLOSURE_ID IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM OPA_DISCL_ACTIVITIES 
    WHERE OPA_DISCLOSURE_ID IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM OPA_OUTSIDE_FIN_INTERESTS 
    WHERE OPA_DISCLOSURE_ID IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM OPA_STUDENT_SUBORDINATE_INVOLVEMENT 
    WHERE OPA_DISCLOSURE_ID IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM COI_QUEST_ANSWER_ATTACHMENT 
    WHERE QUESTIONNAIRE_ANSWER_ID IN (select QUESTIONNAIRE_ANSWER_ID from COI_QUEST_ANSWER 
									WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
																			WHERE MODULE_ITEM_CODE = 23 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID)));
	DELETE FROM COI_QUEST_TABLE_ANSWER 
    WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
											WHERE MODULE_ITEM_CODE = 23 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));
												
	DELETE FROM COI_QUEST_ANSWER 
    WHERE QUESTIONNAIRE_ANS_HEADER_ID IN (select QUESTIONNAIRE_ANS_HEADER_ID FROM quest_answer_header 
											WHERE MODULE_ITEM_CODE = 23 AND MODULE_SUB_ITEM_CODE=0 AND MODULE_ITEM_KEY IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID));
    
    DELETE FROM OPA_DISCLOSURE WHERE OPA_DISCLOSURE_ID IN (SELECT OPA_DISCLOSURE_ID FROM OPA_DISCL_PERSON_ENTITY WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID);
    
    DELETE FROM OPA_DISCL_PERSON_ENTITY 
    WHERE PERSON_ENTITY_ID = AV_PERSON_ENTITY_ID;
    
    SET FOREIGN_KEY_CHECKS=1; 
	SET SQL_SAFE_UPDATES = 1;

END