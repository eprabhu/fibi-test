
DROP TABLE IF EXISTS `prog_rpt_milestones_rt`;

CREATE TABLE `prog_rpt_milestones_rt` (
  `MILESTONES_RT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PROGRESS_REPORT_ID` int(11) NOT NULL DEFAULT '0',
  `REMARK` varchar(2000) DEFAULT NULL,
  `ACTUAL_START_MONTH` date DEFAULT NULL,
  `ACTUAL_END_MONTH` date DEFAULT NULL,
  `START_DATE` datetime DEFAULT NULL,
  `END_DATE` datetime DEFAULT NULL,
  `MILESTONE` varchar(1000) DEFAULT NULL,
  `MILESTONE_STATUS` varchar(200) DEFAULT NULL,
  PRIMARY KEY (`MILESTONES_RT_ID`),
  KEY `IDX_MST_PROG_RPT_ID` (`PROGRESS_REPORT_ID`)
) ;
