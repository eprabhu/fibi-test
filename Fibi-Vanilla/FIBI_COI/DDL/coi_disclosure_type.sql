

--
--  `coi_disclosure_type`
--

DROP TABLE IF EXISTS `coi_disclosure_type`;

CREATE TABLE `coi_disclosure_type` (
  `DISCLOSURE_TYPE_CODE` varchar(3) NOT NULL,
  `DESCRIPTION` varchar(200) DEFAULT NULL,
  `DATA_CAPTURING_TYPE_CODE` int(11) DEFAULT NULL,
  `IS_ACTIVE` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`DISCLOSURE_TYPE_CODE`),
  KEY `COI_DISCLOSURE_TYPE_FK1_idx` (`DATA_CAPTURING_TYPE_CODE`),
  CONSTRAINT `COI_DISCLOSURE_TYPE_FK1` FOREIGN KEY (`DATA_CAPTURING_TYPE_CODE`) REFERENCES `per_ent_data_capturing_type` (`DATA_CAPTURING_TYPE_CODE`)
) ;



