

--
--  `entity_section_attach_ref`
--

DROP TABLE IF EXISTS `entity_section_attach_ref`;

CREATE TABLE `entity_section_attach_ref` (
  `ENTITY_SEC_ATT_REF_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENTITY_ID` int(11) DEFAULT NULL,
  `ENTITY_SECTION_CODE` varchar(3) DEFAULT NULL,
  `ENTITY_ATTACHMENT_ID` int(11) DEFAULT NULL,
  `UPDATE_TIMESTAMP` timestamp NULL DEFAULT NULL,
  `UPDATED_BY` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ENTITY_SEC_ATT_REF_ID`),
  KEY `ENTITY_SEC_ATT_REF_FK1_idx` (`ENTITY_ID`),
  KEY `ENTITY_SEC_ATT_REF_FK2_idx` (`ENTITY_SECTION_CODE`),
  KEY `ENTITY_SEC_ATT_REF_FK3_idx` (`ENTITY_ATTACHMENT_ID`),
  CONSTRAINT `ENTITY_SEC_ATT_REF_FK1` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`),
  CONSTRAINT `ENTITY_SEC_ATT_REF_FK2` FOREIGN KEY (`ENTITY_SECTION_CODE`) REFERENCES `entity_section` (`ENTITY_SECTION_CODE`),
  CONSTRAINT `ENTITY_SEC_ATT_REF_FK3` FOREIGN KEY (`ENTITY_ATTACHMENT_ID`) REFERENCES `entity_attachment` (`ENTITY_ATTACHMENT_ID`)
) ;



