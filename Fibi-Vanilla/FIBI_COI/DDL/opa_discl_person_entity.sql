DROP TABLE IF EXISTS `opa_discl_person_entity`;

CREATE TABLE `opa_discl_person_entity` (
  `OPA_DISCL_PERSON_ENTITY_ID` int(11) NOT NULL AUTO_INCREMENT,
  `OPA_DISCLOSURE_ID` int(11) NOT NULL,
  `PERSON_ENTITY_ID` int(11) DEFAULT NULL,
  `ENTITY_ID` int(11) DEFAULT NULL,
  `ENTITY_NUMBER` int(11) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime NOT NULL,
  `UPDATE_USER` varchar(60) NOT NULL,
  `PERSON_ENTITY_NUMBER` int(11) DEFAULT NULL,
  PRIMARY KEY (`OPA_DISCL_PERSON_ENTITY_ID`),
  KEY `OPA_DISCL_PERSON_ENTITY_FK1` (`OPA_DISCLOSURE_ID`),
  KEY `OPA_DISCL_PERSON_ENTITY_FK2` (`ENTITY_ID`),
  KEY `OPA_DISCL_PERSON_ENTITY_FK3` (`PERSON_ENTITY_ID`),
  CONSTRAINT `OPA_DISCL_PERSON_ENTITY_FK1` FOREIGN KEY (`OPA_DISCLOSURE_ID`) REFERENCES `opa_disclosure` (`OPA_DISCLOSURE_ID`),
  CONSTRAINT `OPA_DISCL_PERSON_ENTITY_FK2` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`),
  CONSTRAINT `OPA_DISCL_PERSON_ENTITY_FK3` FOREIGN KEY (`PERSON_ENTITY_ID`) REFERENCES `person_entity` (`PERSON_ENTITY_ID`)
) ;



