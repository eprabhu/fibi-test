DROP TABLE IF EXISTS `coi_review_comment_tags`;

CREATE TABLE `coi_review_comment_tags` (
  `COI_REVIEW_COMMENT_TAGS_ID` int(11) NOT NULL AUTO_INCREMENT,
  `COI_REVIEW_COMMENT_ID` int(11) DEFAULT NULL,
  `TAG_REF` varchar(20) DEFAULT NULL,
  `TAGGED_PERSON_ID` varchar(40) DEFAULT NULL,
  `TAGGED_GROUP_ID` int(11) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `COI_REVIEW_ID` int(11) DEFAULT NULL,
  PRIMARY KEY (`COI_REVIEW_COMMENT_TAGS_ID`),
  KEY `COI_REVIEW_CMT_TAGS_FK2_idx` (`COI_REVIEW_COMMENT_ID`),
  KEY `COI_REVIEW_CMT_TAGS_FK1` (`COI_REVIEW_ID`),
  CONSTRAINT `COI_REVIEW_CMT_TAGS_FK1` FOREIGN KEY (`COI_REVIEW_ID`) REFERENCES `coi_review` (`COI_REVIEW_ID`),
  CONSTRAINT `COI_REVIEW_CMT_TAGS_FK2` FOREIGN KEY (`COI_REVIEW_COMMENT_ID`) REFERENCES `discl_comment` (`COMMENT_ID`) ON DELETE CASCADE
) ;



