

--
--  `entity_risk`
--

DROP TABLE IF EXISTS `entity_risk`;

CREATE TABLE `entity_risk` (
  `ENTITY_RSIK_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENTITY_ID` int(11) DEFAULT NULL,
  `RISK_TYPE_CODE` varchar(10) DEFAULT NULL,
  `RISK_LEVEL_CODE` varchar(3) DEFAULT NULL,
  `DESCRIPTION` varchar(500) DEFAULT NULL,
  `UPDATE_TIMESTAMP` timestamp NULL DEFAULT NULL,
  `UPDATED_BY` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ENTITY_RSIK_ID`),
  KEY `ENTITY_RISK_FK1_idx` (`ENTITY_ID`),
  KEY `ENTITY_RISK_FK2_idx` (`RISK_TYPE_CODE`),
  KEY `ENTITY_RISK_FK3_idx` (`RISK_LEVEL_CODE`),
  CONSTRAINT `ENTITY_RISK_FK1` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`),
  CONSTRAINT `ENTITY_RISK_FK2` FOREIGN KEY (`RISK_TYPE_CODE`) REFERENCES `entity_risk_type` (`RISK_TYPE_CODE`),
  CONSTRAINT `ENTITY_RISK_FK3` FOREIGN KEY (`RISK_LEVEL_CODE`) REFERENCES `entity_risk_level` (`RISK_LEVEL_CODE`)
) ;



