DROP TABLE IF EXISTS `fibi_coi_person`;

CREATE TABLE `fibi_coi_person` (
  `PERSON_ID` varchar(40) NOT NULL,
  `LAST_NAME` varchar(30) DEFAULT NULL,
  `FIRST_NAME` varchar(30) DEFAULT NULL,
  `MIDDLE_NAME` varchar(30) DEFAULT NULL,
  `FULL_NAME` varchar(90) DEFAULT NULL,
  `PRIOR_NAME` varchar(30) DEFAULT NULL,
  `USER_NAME` varchar(60) DEFAULT NULL,
  `EMAIL_ADDRESS` varchar(60) DEFAULT NULL,
  `DATE_OF_BIRTH` date DEFAULT NULL,
  `AGE` int(11) DEFAULT NULL,
  `AGE_BY_FISCAL_YEAR` int(11) DEFAULT NULL,
  `GENDER` varchar(30) DEFAULT NULL,
  `EDUCATION_LEVEL` varchar(30) DEFAULT NULL,
  `OFFICE_LOCATION` varchar(30) DEFAULT NULL,
  `OFFICE_PHONE` varchar(20) DEFAULT NULL,
  `SECONDRY_OFFICE_LOCATION` varchar(30) DEFAULT NULL,
  `SECONDRY_OFFICE_PHONE` varchar(20) DEFAULT NULL,
  `SCHOOL` varchar(50) DEFAULT NULL,
  `DIRECTORY_DEPARTMENT` varchar(30) DEFAULT NULL,
  `SALUTATION` varchar(30) DEFAULT NULL,
  `COUNTRY_OF_CITIZENSHIP` varchar(30) DEFAULT NULL,
  `PRIMARY_TITLE` varchar(51) DEFAULT NULL,
  `DIRECTORY_TITLE` varchar(50) DEFAULT NULL,
  `HOME_UNIT` varchar(8) DEFAULT NULL,
  `IS_FACULTY` char(1) DEFAULT NULL,
  `IS_GRADUATE_STUDENT_STAFF` char(1) DEFAULT NULL,
  `IS_RESEARCH_STAFF` char(1) DEFAULT NULL,
  `IS_SERVICE_STAFF` char(1) DEFAULT NULL,
  `IS_SUPPORT_STAFF` char(1) DEFAULT NULL,
  `IS_OTHER_ACCADEMIC_GROUP` char(1) DEFAULT NULL,
  `IS_MEDICAL_STAFF` char(1) DEFAULT NULL,
  `ADDRESS_LINE_1` varchar(80) DEFAULT NULL,
  `ADDRESS_LINE_2` varchar(80) DEFAULT NULL,
  `ADDRESS_LINE_3` varchar(80) DEFAULT NULL,
  `CITY` varchar(30) DEFAULT NULL,
  `COUNTY` varchar(30) DEFAULT NULL,
  `STATE` varchar(30) DEFAULT NULL,
  `POSTAL_CODE` varchar(15) DEFAULT NULL,
  `COUNTRY_CODE` varchar(3) DEFAULT NULL,
  `FAX_NUMBER` varchar(20) DEFAULT NULL,
  `PAGER_NUMBER` varchar(20) DEFAULT NULL,
  `MOBILE_PHONE_NUMBER` varchar(20) DEFAULT NULL,
  `VISA_CODE` varchar(20) DEFAULT NULL,
  `VISA_TYPE` varchar(30) DEFAULT NULL,
  `VISA_RENEWAL_DATE` date DEFAULT NULL,
  `STATUS` char(1) NOT NULL,
  `SALARY_ANNIVERSARY_DATE` date DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `VALIDATION_STATUS` varchar(30) DEFAULT 'PENDING',
  `VALIDATION_MESSEGE` varchar(4000) DEFAULT NULL,
  PRIMARY KEY (`PERSON_ID`),
  KEY `idx_validation_status` (`VALIDATION_STATUS`),
  KEY `idx_citizenship_country_homeunit` (`COUNTRY_OF_CITIZENSHIP`,`COUNTRY_CODE`,`HOME_UNIT`)
) ;



