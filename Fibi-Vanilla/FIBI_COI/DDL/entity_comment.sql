

--
--  `entity_comment`
--

DROP TABLE IF EXISTS `entity_comment`;

CREATE TABLE `entity_comment` (
  `ENTITY_COMMENT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENTITY_ID` int(11) DEFAULT NULL,
  `ENTITY_NUMBER` int(11) DEFAULT NULL,
  `COMMENT_TYPE_CODE` varchar(10) DEFAULT NULL,
  `IS_PRIVATE` varchar(1) DEFAULT NULL,
  `COMMENT` text,
  `PARENT_COMMENT_ID` int(11) DEFAULT NULL,
  `UPDATE_TIMESTAMP` timestamp NULL DEFAULT NULL,
  `UPDATED_BY` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ENTITY_COMMENT_ID`),
  KEY `ENTITY_COMMENT_FK1_idx` (`ENTITY_ID`),
  KEY `ENTITY_COMMENT_FK2_idx` (`COMMENT_TYPE_CODE`),
  CONSTRAINT `ENTITY_COMMENT_FK1` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`),
  CONSTRAINT `ENTITY_COMMENT_FK2` FOREIGN KEY (`COMMENT_TYPE_CODE`) REFERENCES `entity_comment_type` (`COMMENT_TYPE_CODE`)
) ;



