DROP TABLE IF EXISTS `coi_project_comment`;

CREATE TABLE `coi_project_comment` (
  `COMMENT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `COMMENT_BY` varchar(40) DEFAULT NULL,
  `PARENT_COMMENT_ID` int(11) DEFAULT NULL,
  `COMMENT` longtext,
  `COMMENT_TYPE_CODE` varchar(3) DEFAULT NULL,
  `IS_PRIVATE` varchar(1) DEFAULT NULL,
  `MODULE_CODE` tinyint(4) DEFAULT NULL,
  `MODULE_ITEM_KEY` varchar(20) DEFAULT NULL,
  `UPDATED_BY` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  PRIMARY KEY (`COMMENT_ID`),
  KEY `COI_PROJECT_COMMENT_FK1` (`COMMENT_BY`),
  KEY `COI_PROJECT_COMMENT_FK2` (`COMMENT_TYPE_CODE`),
  CONSTRAINT `COI_PROJECT_COMMENT_FK1` FOREIGN KEY (`COMMENT_BY`) REFERENCES `person` (`PERSON_ID`),
  CONSTRAINT `COI_PROJECT_COMMENT_FK2` FOREIGN KEY (`COMMENT_TYPE_CODE`) REFERENCES `coi_project_comment_type` (`COMMENT_TYPE_CODE`)
) ;



