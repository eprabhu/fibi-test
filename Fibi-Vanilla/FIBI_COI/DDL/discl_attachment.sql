

--
--  `discl_attachment`
--

DROP TABLE IF EXISTS `discl_attachment`;

CREATE TABLE `discl_attachment` (
  `ATTACHMENT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ATTACHMENT_NUMBER` int(11) DEFAULT NULL,
  `VERSION_NUMBER` int(11) DEFAULT NULL,
  `DISCLOSURE_ID` int(11) DEFAULT NULL,
  `DESCRIPTION` varchar(500) DEFAULT NULL,
  `ATTACHMENT_TYPE_CODE` varchar(3) DEFAULT NULL,
  `FILE_NAME` varchar(200) DEFAULT NULL,
  `MIME_TYPE` varchar(255) DEFAULT NULL,
  `FILE_DATA_ID` varchar(100) DEFAULT NULL,
  `CREATE_TIMESTAMP` datetime DEFAULT NULL,
  `CREATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ATTACHMENT_ID`),
  KEY `DISCL_ATTACHMENT_FK1_idx` (`DISCLOSURE_ID`),
  KEY `DISCL_ATTACHMENT_FK2_idx` (`ATTACHMENT_TYPE_CODE`),
  CONSTRAINT `DISCL_ATTACHMENT_FK1` FOREIGN KEY (`DISCLOSURE_ID`) REFERENCES `coi_disclosure` (`DISCLOSURE_ID`),
  CONSTRAINT `DISCL_ATTACHMENT_FK2` FOREIGN KEY (`ATTACHMENT_TYPE_CODE`) REFERENCES `discl_atta_type` (`ATTA_TYPE_CODE`)
) ;



