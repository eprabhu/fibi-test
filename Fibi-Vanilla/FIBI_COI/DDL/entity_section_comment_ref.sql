

--
--  `entity_section_comment_ref`
--

DROP TABLE IF EXISTS `entity_section_comment_ref`;

CREATE TABLE `entity_section_comment_ref` (
  `ENTITY_SEC_COM_REF_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENTITY_ID` int(11) DEFAULT NULL,
  `ENTITY_NUMBER` int(11) DEFAULT NULL,
  `ENTITY_SECTION_CODE` varchar(3) DEFAULT NULL,
  `ENTITY_COMMENT_ID` int(11) DEFAULT NULL,
  `UPDATE_TIMESTAMP` timestamp NULL DEFAULT NULL,
  `UPDATED_BY` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ENTITY_SEC_COM_REF_ID`),
  KEY `ENTITY_SEC_COM_REF_FK1_idx` (`ENTITY_ID`),
  KEY `ENTITY_SEC_COM_REF_FK2_idx` (`ENTITY_SECTION_CODE`),
  KEY `ENTITY_SEC_COM_REF_FK3_idx` (`ENTITY_COMMENT_ID`),
  CONSTRAINT `ENTITY_SEC_COM_REF_FK1` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`),
  CONSTRAINT `ENTITY_SEC_COM_REF_FK2` FOREIGN KEY (`ENTITY_SECTION_CODE`) REFERENCES `entity_section` (`ENTITY_SECTION_CODE`),
  CONSTRAINT `ENTITY_SEC_COM_REF_FK3` FOREIGN KEY (`ENTITY_COMMENT_ID`) REFERENCES `entity_comment` (`ENTITY_COMMENT_ID`)
) ;



