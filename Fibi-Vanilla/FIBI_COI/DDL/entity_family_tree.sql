DROP TABLE IF EXISTS `entity_family_tree`;

CREATE TABLE `entity_family_tree` (
  `ENTITY_FAMILY_TREE_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENTITY_ID` int(11) DEFAULT NULL,
  `PARENT_ENTITY_ID` int(11) DEFAULT NULL,
  `GLOBAL_ULTIMATE_ENTITY_ID` int(11) DEFAULT NULL,
  `UPDATED_BY` varchar(40) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  PRIMARY KEY (`ENTITY_FAMILY_TREE_ID`),
  KEY `ENTITY_FAMILY_TREE_FK1_idx` (`ENTITY_ID`),
  KEY `ENTITY_FAMILY_TREE_FK2_idx` (`PARENT_ENTITY_ID`),
  CONSTRAINT `ENTITY_FAMILY_TREE_FK1` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`),
  CONSTRAINT `ENTITY_FAMILY_TREE_FK2` FOREIGN KEY (`PARENT_ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`)
) ;



