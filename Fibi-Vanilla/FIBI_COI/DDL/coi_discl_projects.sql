DROP TABLE IF EXISTS `coi_discl_projects`;

CREATE TABLE `coi_discl_projects` (
  `COI_DISCL_PROJECTS_ID` int(11) NOT NULL AUTO_INCREMENT,
  `DISCLOSURE_ID` int(11) DEFAULT NULL,
  `DISCLOSURE_NUMBER` int(11) DEFAULT NULL,
  `MODULE_CODE` varchar(10) DEFAULT NULL,
  `MODULE_ITEM_KEY` varchar(45) DEFAULT NULL,
  `UPDATED_BY` varchar(40) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `PROJECT_SNAPSHOT` json DEFAULT NULL,
  PRIMARY KEY (`COI_DISCL_PROJECTS_ID`),
  KEY `COI_DISCL_PROJECTS_FK1_idx` (`DISCLOSURE_ID`),
  CONSTRAINT `COI_DISCL_PROJECTS_FK1` FOREIGN KEY (`DISCLOSURE_ID`) REFERENCES `coi_disclosure` (`DISCLOSURE_ID`)
) ;



