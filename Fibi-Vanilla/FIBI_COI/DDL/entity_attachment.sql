

--
--  `entity_attachment`
--

DROP TABLE IF EXISTS `entity_attachment`;

CREATE TABLE `entity_attachment` (
  `ENTITY_ATTACHMENT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ATTACHMENT_NUMBER` int(11) DEFAULT NULL,
  `VERSION_NUMBER` int(11) DEFAULT NULL,
  `VERSION_STATUS` varchar(45) DEFAULT NULL,
  `ENTITY_ID` int(11) DEFAULT NULL,
  `COMMENT` varchar(2000) DEFAULT NULL,
  `ATTACHMENT_TYPE_CODE` varchar(10) DEFAULT NULL,
  `ATTACHMENT_STATUS_CODE` varchar(3) DEFAULT NULL,
  `FILE_NAME` varchar(200) DEFAULT NULL,
  `MIME_TYPE` varchar(255) DEFAULT NULL,
  `FILE_DATA_ID` varchar(100) DEFAULT NULL,
  `UPDATE_TIMESTAMP` timestamp NULL DEFAULT NULL,
  `UPDATED_BY` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ENTITY_ATTACHMENT_ID`),
  KEY `ENTITY_ATTACH_FK1_idx` (`ENTITY_ID`),
  KEY `ENTITY_ATTACH_FK2_idx` (`ATTACHMENT_TYPE_CODE`),
  KEY `ENTITY_ATTACH_FK3_idx` (`ATTACHMENT_STATUS_CODE`),
  CONSTRAINT `ENTITY_ATTACH_FK1` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`),
  CONSTRAINT `ENTITY_ATTACH_FK2` FOREIGN KEY (`ATTACHMENT_TYPE_CODE`) REFERENCES `entity_attachment_type` (`ATTACHMENT_TYPE_CODE`),
  CONSTRAINT `ENTITY_ATTACH_FK3` FOREIGN KEY (`ATTACHMENT_STATUS_CODE`) REFERENCES `entity_attachment_status_type` (`ATTACHMENT_STATUS_CODE`)
) ;



