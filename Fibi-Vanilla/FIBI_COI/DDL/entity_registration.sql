

--
--  `entity_registration`
--

DROP TABLE IF EXISTS `entity_registration`;

CREATE TABLE `entity_registration` (
  `ENTITY_REGISTRATION_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENTITY_ID` int(11) DEFAULT NULL,
  `REG_TYPE_CODE` varchar(10) DEFAULT NULL,
  `REG_NUMBER` varchar(50) DEFAULT NULL,
  `IS_ACTIVE` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` timestamp NULL DEFAULT NULL,
  `UPDATED_BY` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ENTITY_REGISTRATION_ID`),
  KEY `ENTITY_REGISTRATION_FK1_idx` (`ENTITY_ID`),
  KEY `ENTITY_REGISTRATION_FK2_idx` (`REG_TYPE_CODE`),
  CONSTRAINT `ENTITY_REGISTRATION_FK1` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`),
  CONSTRAINT `ENTITY_REGISTRATION_FK2` FOREIGN KEY (`REG_TYPE_CODE`) REFERENCES `entity_registration_type` (`REG_TYPE_CODE`)
) ;



