

--
--  `discl_comment`
--

DROP TABLE IF EXISTS `discl_comment`;

CREATE TABLE `discl_comment` (
  `COMMENT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `DOCUMENT_OWNER_PERSON_ID` varchar(45) DEFAULT NULL,
  `COMMENT_BY_PERSON_ID` varchar(45) DEFAULT NULL,
  `COMMENT_TYPE_CODE` varchar(3) DEFAULT NULL,
  `COMPONENT_TYPE_CODE` varchar(3) DEFAULT NULL,
  `PARENT_COMMENT_ID` int(11) DEFAULT NULL,
  `IS_PRIVATE` varchar(1) DEFAULT NULL,
  `COMMENT` text,
  `MODULE_ITEM_KEY` int(11) DEFAULT NULL,
  `MODULE_ITEM_NUMBER` varchar(11) DEFAULT NULL,
  `SUB_MODULE_ITEM_KEY` varchar(12) DEFAULT NULL,
  `SUB_MODULE_ITEM_NUMBER` varchar(11) DEFAULT NULL,
  `MODULE_CODE` int(11) NOT NULL,
  `SUB_MODULE_CODE` int(11) DEFAULT NULL,
  `FORM_BUILDER_ID` int(11) DEFAULT NULL,
  `FORM_BUILDER_SECTION_ID` int(11) DEFAULT NULL,
  `FORM_BUILDER_COMPONENT_ID` int(11) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  PRIMARY KEY (`COMMENT_ID`),
  KEY `DISCL_COMMENT_FK1_idx` (`COMPONENT_TYPE_CODE`),
  KEY `DISCL_COMMENT_FK2_idx` (`COMMENT_TYPE_CODE`),
  CONSTRAINT `DISCL_COMMENT_FK1` FOREIGN KEY (`COMPONENT_TYPE_CODE`) REFERENCES `discl_component_type` (`COMPONENT_TYPE_CODE`),
  CONSTRAINT `DISCL_COMMENT_FK2` FOREIGN KEY (`COMMENT_TYPE_CODE`) REFERENCES `discl_comment_type` (`COMMENT_TYPE_CODE`)
) ;



