

--
--  `review_attachment`
--

DROP TABLE IF EXISTS `review_attachment`;

CREATE TABLE `review_attachment` (
  `ATTACHMENT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `COMMENT_ID` int(11) DEFAULT NULL,
  `COMPONENT_TYPE_CODE` varchar(1) DEFAULT NULL,
  `COMPONENT_REFERENCE_ID` int(11) DEFAULT NULL,
  `COMPONENT_REFERENCE_NUMBER` varchar(45) DEFAULT NULL,
  `ATTACHMENT_NUMBER` int(11) DEFAULT NULL,
  `VERSION_NUMBER` int(11) DEFAULT NULL,
  `VERSION_STATUS` varchar(45) DEFAULT NULL,
  `ATTA_TYPE_CODE` varchar(1) DEFAULT NULL,
  `ATTA_STATUS_CODE` varchar(1) DEFAULT NULL,
  `DOCUMENT_OWNER_PERSON_ID` varchar(45) DEFAULT NULL,
  `FILE_NAME` varchar(45) DEFAULT NULL,
  `MIME_TYPE` varchar(255) DEFAULT NULL,
  `DESCRIPTION` varchar(500) DEFAULT NULL,
  `FILE_DATA_ID` int(11) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ATTACHMENT_ID`),
  KEY `REVIEW_ATTACHMENT_FK1_idx` (`COMMENT_ID`),
  KEY `REVIEW_ATTACHMENT_FK2_idx` (`COMPONENT_TYPE_CODE`),
  KEY `REVIEW_ATTACHMENT_FK3_idx` (`ATTA_TYPE_CODE`),
  KEY `REVIEW_ATTACHMENT_FK4_idx` (`ATTA_STATUS_CODE`),
  CONSTRAINT `REVIEW_ATTACHMENT_FK1` FOREIGN KEY (`COMMENT_ID`) REFERENCES `discl_comment` (`COMMENT_ID`),
  CONSTRAINT `REVIEW_ATTACHMENT_FK2` FOREIGN KEY (`COMPONENT_TYPE_CODE`) REFERENCES `discl_component_type` (`COMPONENT_TYPE_CODE`),
  CONSTRAINT `REVIEW_ATTACHMENT_FK3` FOREIGN KEY (`ATTA_TYPE_CODE`) REFERENCES `discl_atta_type` (`ATTA_TYPE_CODE`),
  CONSTRAINT `REVIEW_ATTACHMENT_FK4` FOREIGN KEY (`ATTA_STATUS_CODE`) REFERENCES `discl_atta_status` (`ATTA_STATUS_CODE`)
) ;



