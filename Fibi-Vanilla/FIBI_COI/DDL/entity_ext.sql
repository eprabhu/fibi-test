DROP TABLE IF EXISTS `entity_ext`;

CREATE TABLE `entity_ext` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `ENTITY_ID` int(11) NOT NULL,
  `COL_1_STRING_LABEL` varchar(90) DEFAULT NULL,
  `COL_1_STRING_VALUE` varchar(90) DEFAULT NULL,
  `COL_2_STRING_LABEL` varchar(90) DEFAULT NULL,
  `COL_2_STRING_VALUE` varchar(90) DEFAULT NULL,
  `COL_3_STRING_LABEL` varchar(90) DEFAULT NULL,
  `COL_3_STRING_VALUE` varchar(90) DEFAULT NULL,
  `COL_4_STRING_LABEL` varchar(90) DEFAULT NULL,
  `COL_4_STRING_VALUE` varchar(90) DEFAULT NULL,
  `COL_5_DATE_LABEL` varchar(90) DEFAULT NULL,
  `COL_5_DATE_VALUE` date DEFAULT NULL,
  `COL_6_DATE_LABEL` varchar(90) DEFAULT NULL,
  `COL_6_DATE_VALUE` date DEFAULT NULL,
  `COL_7_DATE_LABEL` varchar(90) DEFAULT NULL,
  `COL_7_DATE_VALUE` date DEFAULT NULL,
  PRIMARY KEY (`ID`),
  KEY `ENTITY_EXT_FK1_idx` (`ENTITY_ID`),
  CONSTRAINT `ENTITY_EXT_FK1` FOREIGN KEY (`ENTITY_ID`) REFERENCES `entity` (`ENTITY_ID`)
) ;



