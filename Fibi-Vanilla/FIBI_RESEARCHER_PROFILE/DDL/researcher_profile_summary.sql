
DROP TABLE IF EXISTS `researcher_profile_summary`;

CREATE TABLE `researcher_profile_summary` (
  `RESEARCHER_PROFILE_SUMMARY_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PERSON_ID` varchar(40) NOT NULL,
  `DESCRIPTION` text,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_PROFILE_SUMMARY_ID`),
  KEY `RESR_PROFILE_SUMMARY_FK1` (`PERSON_ID`),
  CONSTRAINT `RESR_PROFILE_SUMMARY_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`)
) ;
