
DROP TABLE IF EXISTS `researcher_profile_dyn_subsection_config`;

CREATE TABLE `researcher_profile_dyn_subsection_config` (
  `DYN_SUBSECTION_CONFIG_ID` int(11) NOT NULL AUTO_INCREMENT,
  `SECTION_CODE` varchar(6) DEFAULT NULL,
  `PERSON_ID` varchar(40) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `IS_PRIVATE` varchar(1) DEFAULT NULL,
  PRIMARY KEY (`DYN_SUBSECTION_CONFIG_ID`),
  KEY `RESRCHER_PROFILE_SECTION_CONFIG_FK1` (`PERSON_ID`),
  CONSTRAINT `RESRCHER_PROFILE_SECTION_CONFIG_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`)
) ;
