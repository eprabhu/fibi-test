
DROP TABLE IF EXISTS `researcher_social_profile`;

CREATE TABLE `researcher_social_profile` (
  `RESEARCHER_SOCIAL_PROFILE_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PERSON_ID` varchar(40) NOT NULL,
  `WEBSITE_URL` varchar(255) DEFAULT NULL,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `URL_ALIAS` varchar(100) DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_SOCIAL_PROFILE_ID`),
  KEY `RESR_SOCIAL_PROFILE_FK1` (`PERSON_ID`),
  CONSTRAINT `RESR_SOCIAL_PROFILE_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`)
) ;
