DROP TABLE IF EXISTS `researcher_affiliation`;

CREATE TABLE `researcher_affiliation` (
  `RESEARCHER_AFFILIATION_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PERSON_ID` varchar(40) NOT NULL,
  `TITLE` varchar(255) DEFAULT NULL,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_AFFILIATION_ID`),
  KEY `RESEARCHER_AFFILIATION_FK1` (`PERSON_ID`),
  CONSTRAINT `RESEARCHER_AFFILIATION_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`)
) ;
