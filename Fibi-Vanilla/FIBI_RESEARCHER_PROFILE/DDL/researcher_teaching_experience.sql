
DROP TABLE IF EXISTS `researcher_teaching_experience`;

CREATE TABLE `researcher_teaching_experience` (
  `RESEARCHER_TEACHING_EXP_ID` int(11) NOT NULL AUTO_INCREMENT,
  `SUBJECT` varchar(200) DEFAULT NULL,
  `INSTITUTION` varchar(200) DEFAULT NULL,
  `PERSON_ID` varchar(40) NOT NULL,
  `RESPONSIBILITY` text,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `START_DATE` date DEFAULT NULL,
  `END_DATE` date DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_TEACHING_EXP_ID`),
  KEY `RESER_TEACHING_EXP_FK1` (`PERSON_ID`),
  CONSTRAINT `RESER_TEACHING_EXP_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`)
) ;