
DROP TABLE IF EXISTS `researcher_professional_membership`;

CREATE TABLE `researcher_professional_membership` (
  `RESEARCHER_PROF_MEM_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PERSON_ID` varchar(40) NOT NULL,
  `TITLE` varchar(255) DEFAULT NULL,
  `DESCRIPTION` text,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `START_YEAR` int(11) DEFAULT NULL,
  `END_YEAR` int(11) DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_PROF_MEM_ID`),
  KEY `RESER_PROF_MEMBERSHIP_FK1` (`PERSON_ID`),
  CONSTRAINT `RESER_PROF_MEMBERSHIP_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`)
) ;
