
DROP TABLE IF EXISTS `researcher_conference_detail`;

CREATE TABLE `researcher_conference_detail` (
  `RESEARCHER_CONF_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PERSON_ID` varchar(40) NOT NULL,
  `TITLE` varchar(255) DEFAULT NULL,
  `NAME` varchar(255) DEFAULT NULL,
  `DESCRIPTION` text,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `PRESENTATION_YEAR` date DEFAULT NULL,
  `LOCATION` varchar(80) DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_CONF_ID`),
  KEY `RESEARCHER_CONFERENCE_DETAIL_FK1` (`PERSON_ID`),
  CONSTRAINT `RESEARCHER_CONFERENCE_DETAIL_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`)
) ;
