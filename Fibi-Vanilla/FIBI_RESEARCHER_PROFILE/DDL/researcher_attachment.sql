
DROP TABLE IF EXISTS `researcher_attachment`;
CREATE TABLE `researcher_attachment` (
  `RESEARCHER_ATTACHMENT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PERSON_ID` varchar(40) DEFAULT NULL,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `RESEARCHER_ATTACH_TYPE_CODE` varchar(3) DEFAULT NULL,
  `FILE_DATA_ID` varchar(100) DEFAULT NULL,
  `DESCRIPTION` varchar(200) DEFAULT NULL,
  `FILE_NAME` varchar(500) DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_ATTACHMENT_ID`),
  KEY `RESEARCHER_ATTACHMENT_FK1` (`PERSON_ID`),
  KEY `RESEARCHER_ATTACHMENT_FK2` (`RESEARCHER_ATTACH_TYPE_CODE`),
  CONSTRAINT `RESEARCHER_ATTACHMENT_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`),
  CONSTRAINT `RESEARCHER_ATTACHMENT_FK2` FOREIGN KEY (`RESEARCHER_ATTACH_TYPE_CODE`) REFERENCES `researcher_attachment_type` (`RESEARCHER_ATTACH_TYPE_CODE`)
) ;