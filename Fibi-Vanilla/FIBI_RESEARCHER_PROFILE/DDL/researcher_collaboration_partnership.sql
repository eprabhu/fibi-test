
DROP TABLE IF EXISTS `researcher_collaboration_partnership`;

CREATE TABLE `researcher_collaboration_partnership` (
  `RESEARCHER_COLLAB_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PERSON_ID` varchar(40) NOT NULL,
  `DESCRIPTION` text,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `COLLABORATOR_NAME` varchar(255) DEFAULT NULL,
  `END_YEAR` date DEFAULT NULL,
  `INSTITUTE` varchar(255) DEFAULT NULL,
  `START_YEAR` date DEFAULT NULL,
  PRIMARY KEY (`RESEARCHER_COLLAB_ID`),
  KEY `RESR_COLLABORATION_PARTNERSHIP_FK1` (`PERSON_ID`),
  CONSTRAINT `RESR_COLLABORATION_PARTNERSHIP_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`)
) ;
