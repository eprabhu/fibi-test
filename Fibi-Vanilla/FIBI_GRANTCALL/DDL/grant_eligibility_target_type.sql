
DROP TABLE IF EXISTS `grant_eligibility_target_type`;

CREATE TABLE `grant_eligibility_target_type` (
  `ELIGIBILITY_TARGET_TYPE_CODE` varchar(3) NOT NULL,
  `TARGET_CATEGORY_TYPE_CODE` varchar(3) DEFAULT NULL,
