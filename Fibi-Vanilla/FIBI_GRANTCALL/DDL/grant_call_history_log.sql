
DROP TABLE IF EXISTS `grant_call_history_log`;

CREATE TABLE `grant_call_history_log` (
  `GRANT_CALL_HISTORY_LOG_ID` int(11) NOT NULL AUTO_INCREMENT,
  `GRANT_HEADER_ID` int(11) NOT NULL,
  `CLOSING_DATE` datetime DEFAULT NULL,
  `INTERNAL_DEADLINE_DATE` datetime DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`GRANT_CALL_HISTORY_LOG_ID`)
) ;