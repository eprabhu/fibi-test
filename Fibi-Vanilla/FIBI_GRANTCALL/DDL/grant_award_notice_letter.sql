
DROP TABLE IF EXISTS `grant_award_notice_letter`;

CREATE TABLE `grant_award_notice_letter` (
  `GRANT_AWARD_NOTICE_LETTER_ID` int(11) NOT NULL,
  `GRANT_CALL_TITLE` varchar(255) DEFAULT NULL,
  `ACTIVITY_TYPE_CODE` varchar(255) DEFAULT NULL,
  `LETTER_TEMPLATE_TYPE_CODE` int(11) DEFAULT NULL,
  `UPDATE_TIMESTAMP` timestamp NULL DEFAULT NULL,
  `UPDATE_USER` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`GRANT_AWARD_NOTICE_LETTER_ID`),
  UNIQUE KEY `GRANT_CALL_TITLE` (`GRANT_CALL_TITLE`,`ACTIVITY_TYPE_CODE`)
) ;
