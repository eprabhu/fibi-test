DROP TABLE IF EXISTS `notify_placeholder_columns`;

  CREATE TABLE `NOTIFY_PLACEHOLDER_COLUMNS` (
  `NOTIFY_PLACEHOLDER_COLUMN_ID` INT NOT NULL AUTO_INCREMENT,
  `NOTIFY_PLACEHOLDER_HEADER_ID` INT NOT NULL,
  `QUERY_COLUMN_NAME` VARCHAR(100) NOT NULL,
  `LABEL_NAME` VARCHAR(100) NOT NULL,
  `UPDATE_TIMESTAMP` DATETIME NULL,
  `UPDATE_USER` VARCHAR(60) NULL,
  PRIMARY KEY (`NOTIFY_PLACEHOLDER_COLUMN_ID`),
  INDEX `NOTIFY_PLACEHOLDER_COLUMNS_FK1_idx` (`NOTIFY_PLACEHOLDER_HEADER_ID` ASC) VISIBLE,
  CONSTRAINT `NOTIFY_PLACEHOLDER_COLUMNS_FK1`
    FOREIGN KEY (`NOTIFY_PLACEHOLDER_HEADER_ID`)
    REFERENCES `NOTIFY_PLACEHOLDER_HEADER` (`NOTIFY_PLACEHOLDER_HEADER_ID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION);
