

--
--  `assoc_module_mapping`
--

DROP TABLE IF EXISTS `assoc_module_mapping`;

CREATE TABLE `assoc_module_mapping` (
  `ASSOC_MAPPING_CODE` varchar(3) NOT NULL,
  `LABEL` varchar(200) DEFAULT NULL,
  `APPLIED_MODULE_CODE` int(11) DEFAULT NULL,
  `ASSOC_CONFIG_CODE` varchar(3) DEFAULT NULL,
  `HELP_TEXT` varchar(2000) DEFAULT NULL,
  `IS_ACTIVE` varchar(1) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  PRIMARY KEY (`ASSOC_MAPPING_CODE`),
  KEY `ASSOC_MODULE_MAPPING_FK1` (`ASSOC_CONFIG_CODE`),
  CONSTRAINT `ASSOC_MODULE_MAPPING_FK1` FOREIGN KEY (`ASSOC_CONFIG_CODE`) REFERENCES `assoc_config` (`ASSOC_CONFIG_CODE`)
);

