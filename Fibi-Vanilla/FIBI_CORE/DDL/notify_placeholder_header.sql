DROP TABLE IF EXISTS `notify_placeholder_header`;

CREATE TABLE `NOTIFY_PLACEHOLDER_HEADER` (
  `NOTIFY_PLACEHOLDER_HEADER_ID` INT NOT NULL AUTO_INCREMENT,
  `MODULE_CODE` INT(3) NOT NULL,
  `SUB_MODULE_CODE` INT(3) NOT NULL,
  `QUERY_DEFINITION` TEXT NULL,
  `QUERY_TYPE` VARCHAR(1) NOT NULL,
  `ELEMENT_NAME` VARCHAR(100) NULL,
  `ELEMENT_TYPE` VARCHAR(1) NOT NULL,
  `IS_ACTIVE` VARCHAR(1) NOT NULL,
  `DESCRIPTION` VARCHAR(200) NULL,
  `UPDATE_TIMESTAMP` DATETIME NULL,
  `UPDATE_USER` VARCHAR(60) NULL,
  PRIMARY KEY (`NOTIFY_PLACEHOLDER_HEADER_ID`));
