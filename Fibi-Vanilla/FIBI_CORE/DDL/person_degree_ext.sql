DROP TABLE IF EXISTS `person_degree_ext`;

CREATE TABLE `person_degree_ext` (
  `PERSON_DEGREE_EXT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `PERSON_DEGREE_ID` int(11) DEFAULT NULL,
  `STATUS` varchar(1) DEFAULT NULL,
  `SORT_ORDER` int(11) DEFAULT NULL,
  `IS_HIGHLIGHTED` varchar(1) DEFAULT NULL,
  `DESCRIPTION` text,
  `SOURCE_TYPE_CODE` varchar(3) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`PERSON_DEGREE_EXT_ID`),
  KEY `PERSON_DEGREE_EXT_FK1` (`PERSON_DEGREE_ID`),
  KEY `PERSON_DEGREE_EXT_FK2` (`SOURCE_TYPE_CODE`),
  CONSTRAINT `PERSON_DEGREE_EXT_FK1` FOREIGN KEY (`PERSON_DEGREE_ID`) REFERENCES `person_degree` (`PERSON_DEGREE_ID`),
  CONSTRAINT `PERSON_DEGREE_EXT_FK2` FOREIGN KEY (`SOURCE_TYPE_CODE`) REFERENCES `source_type` (`SOURCE_TYPE_CODE`)
);

