DROP TABLE IF EXISTS `mq_router_action_configuration`;

CREATE TABLE `mq_router_action_configuration` (
  `MQ_ROUTER_ACTION_CONFIG_ID` int(11) NOT NULL AUTO_INCREMENT,
  `MODULE_CODE` int(11) DEFAULT NULL,
  `SUB_MODULE_CODE` int(11) DEFAULT NULL,
  `ACTION_TYPE` varchar(200) DEFAULT NULL,
  `QUEUE_NAME` varchar(255) DEFAULT NULL,
  `IS_ACTIVE` varchar(1) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`MQ_ROUTER_ACTION_CONFIG_ID`)
);