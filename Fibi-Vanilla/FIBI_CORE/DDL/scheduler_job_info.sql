DROP TABLE IF EXISTS `scheduler_job_info`;

CREATE TABLE `scheduler_job_info` (
  `JOB_ID` int(11) NOT NULL AUTO_INCREMENT,
  `CRON_EXPRESSION` varchar(60) DEFAULT NULL,
  `IS_CRON_JOB` varchar(1) DEFAULT NULL,
  `JOB_CLASS` varchar(255) DEFAULT NULL,
  `JOB_GROUP` varchar(255) DEFAULT NULL,
  `JOB_NAME` varchar(255) DEFAULT NULL,
  `REPEAT_TIME` int(11) DEFAULT NULL,
  `DESCRIPTION` varchar(1000) DEFAULT NULL,
  `CRON_STATUS` varchar(50) DEFAULT NULL,
  `UPDATE_USER` varchar(50) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `PREVIOUS_FIRE_TIME` datetime DEFAULT NULL,
  `LAST_FIRE_END_TIME` datetime DEFAULT NULL,
  `IS_ACTIVE` varchar(1) DEFAULT NULL,
  PRIMARY KEY (`JOB_ID`)
) ;
