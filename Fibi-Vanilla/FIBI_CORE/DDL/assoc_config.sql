

--
--  `assoc_config`
--

DROP TABLE IF EXISTS `assoc_config`;

CREATE TABLE `assoc_config` (
  `ASSOC_CONFIG_CODE` varchar(3) NOT NULL,
  `NAME` varchar(200) DEFAULT NULL,
  `SEARCH_INDEX` varchar(2000) DEFAULT NULL,
  `SEARCH_CONFIG` longtext,
  `DATA_SOURCE` varchar(200) DEFAULT NULL,
  `ASSOC_MODULE_CODE` int(11) DEFAULT NULL,
  `ASSOCIATION_URL` varchar(1000) DEFAULT NULL,
  `REVERSE_RELATION_SOURCE` varchar(1000) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`ASSOC_CONFIG_CODE`)
);

