DROP TABLE IF EXISTS `medusa_relation`;

CREATE TABLE `medusa_relation` (
  `MEDUSA_RELATION_ID` int NOT NULL AUTO_INCREMENT,
  `SOURCE_MODULE` varchar(50) NOT NULL,
  `DESTINATION_MODULE` varchar(50) NOT NULL,
  `RELATION_NAME` varchar(50) NOT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`MEDUSA_RELATION_ID`)
) ;
