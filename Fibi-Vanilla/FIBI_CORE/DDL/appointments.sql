

--
--  `appointments`
--

DROP TABLE IF EXISTS `appointments`;

CREATE TABLE `appointments` (
  `APPOINTMENTS_ID` int PRIMARY KEY AUTO_INCREMENT,
  `PERSON_ID` varchar(40) NOT NULL,
  `UNIT_NUMBER` varchar(8) NOT NULL,
  `PRIMARY_SECONDARY_INDICATOR` varchar(30) DEFAULT NULL,
  `APPOINTMENT_START_DATE` date DEFAULT NULL,
  `APPOINTMENT_END_DATE` date DEFAULT NULL,
  `APPOINTMENT_TYPE` varchar(30) DEFAULT NULL,
  `JOB_TITLE` varchar(50) DEFAULT NULL,
  `PREFERED_JOB_TITLE` varchar(51) DEFAULT NULL,
  `JOB_CODE` varchar(6) NOT NULL,
  `SALARY` decimal(12,2) DEFAULT NULL,
  `UPDATE_TIMESTAMP` date NOT NULL,
  `UPDATE_USER` varchar(60) NOT NULL,
  KEY `APPOINTMENTS_FK1` (`JOB_CODE`),
  KEY `APPOINTMENTS_FK2` (`UNIT_NUMBER`),
  CONSTRAINT `APPOINTMENTS_IBFK_1` FOREIGN KEY (`JOB_CODE`) REFERENCES `job_code` (`JOB_CODE`),
  CONSTRAINT `APPOINTMENTS_IBFK_2` FOREIGN KEY (`UNIT_NUMBER`) REFERENCES `unit` (`UNIT_NUMBER`)
);
