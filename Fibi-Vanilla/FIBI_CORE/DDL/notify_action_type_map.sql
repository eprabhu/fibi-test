

--
--  `notify_action_type_map`
--

DROP TABLE IF EXISTS `notify_action_type_map`;

CREATE TABLE `notify_action_type_map` (
  `NOTIFY_ACTION_TYPE_MAP_ID` int(11) NOT NULL AUTO_INCREMENT,
  `NOTIFICATION_TYPE_ID` int(11) NOT NULL,
  `MODULE_ACTION_TYPE_ID` int(11) NOT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`NOTIFY_ACTION_TYPE_MAP_ID`),
  KEY `NOTIFY_ACTION_TYPE_MAP_FK1` (`NOTIFICATION_TYPE_ID`),
  KEY `NOTIFY_ACTION_TYPE_MAP_FK2` (`MODULE_ACTION_TYPE_ID`),
  CONSTRAINT `NOTIFY_ACTION_TYPE_MAP_FK1` FOREIGN KEY (`NOTIFICATION_TYPE_ID`) REFERENCES `notification_type` (`NOTIFICATION_TYPE_ID`),
  CONSTRAINT `NOTIFY_ACTION_TYPE_MAP_FK2` FOREIGN KEY (`MODULE_ACTION_TYPE_ID`) REFERENCES `module_action_type` (`MODULE_ACTION_TYPE_ID`)
);

