DROP TABLE IF EXISTS `mail_reader_type_config`;

CREATE TABLE `mail_reader_type_config` (
  `MAIL_TYPE_ID` int(11) NOT NULL AUTO_INCREMENT,
  `MAIL_PROVIDER` varchar(45) DEFAULT NULL,
  `CLIENT_ID` varchar(255) DEFAULT NULL,
  `CLIENT_SECRET` varchar(255) DEFAULT NULL,
  `TENTANT_ID` varchar(255) DEFAULT NULL,
  `SCOPE` varchar(255) DEFAULT NULL,
  `MAIL_FETCH_LIMIT` int(11) DEFAULT NULL,
  `REPROCESS_COUNT` int(11) DEFAULT NULL,
  `ALLOWED_FILE_TYPE` varchar(225) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime(6) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`MAIL_TYPE_ID`)
) ;