DROP TABLE IF EXISTS `mail_reader_client_parameter`;

CREATE TABLE `mail_reader_client_parameter` (
  `CLIENT_PARAM_ID` int(11) NOT NULL AUTO_INCREMENT,
  `USER_MAIL_ADDRESS` varchar(255) DEFAULT NULL,
  `MAIL_TYPE_ID` int(11) DEFAULT NULL,
  `AUTH_CODE` mediumtext,
  `ACCESS_TOKEN` mediumtext,
  `REFRESH_TOKEN` mediumtext,
  `REDIRECT_URI` varchar(45) DEFAULT NULL,
  `PROCESSED_EMAIL_FOLDER` varchar(45) DEFAULT NULL,
  `UNPROCESSED_EMAIL_FOLDER_NAME` varchar(45) DEFAULT NULL,
  `UNPROCESSED_EMAIL_FOLDER_ID` varchar(500) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime(6) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`CLIENT_PARAM_ID`),
  KEY `MAIL_READER_CLIENT_PARAMETER_FK1` (`MAIL_TYPE_ID`),
  CONSTRAINT `MAIL_READER_CLIENT_PARAMETER_FK1` FOREIGN KEY (`MAIL_TYPE_ID`) REFERENCES `mail_reader_type_config` (`MAIL_TYPE_ID`)
) ;