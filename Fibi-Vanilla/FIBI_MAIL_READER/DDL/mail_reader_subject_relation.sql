DROP TABLE IF EXISTS `mail_reader_subject_relation`;
CREATE TABLE `mail_reader_subject_relation` (
  `RELATION_ID` int(11) NOT NULL AUTO_INCREMENT,
  `SUBJECT` varchar(500) DEFAULT NULL,
  `TYPE_CODE` varchar(3) DEFAULT NULL,
  `UNIT_NUMBER` varchar(8) DEFAULT NULL,
  `CLIENT_PARAM_ID` int(11) DEFAULT NULL,
  `DEFAULT_PERSON_ID` varchar(45) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime(6) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`RELATION_ID`),
  KEY `MAIL_READER_SUBJECT_RELATION_FK2` (`TYPE_CODE`),
  KEY `MAIL_READER_SUBJECT_RELATION_FK3` (`UNIT_NUMBER`),
  KEY `MAIL_READER_SUBJECT_RELATION_FK1` (`CLIENT_PARAM_ID`),
  CONSTRAINT `MAIL_READER_SUBJECT_RELATION_FK1` FOREIGN KEY (`CLIENT_PARAM_ID`) REFERENCES `mail_reader_client_parameter` (`CLIENT_PARAM_ID`),
  CONSTRAINT `MAIL_READER_SUBJECT_RELATION_FK2` FOREIGN KEY (`TYPE_CODE`) REFERENCES `sr_type` (`TYPE_CODE`),
  CONSTRAINT `MAIL_READER_SUBJECT_RELATION_FK3` FOREIGN KEY (`UNIT_NUMBER`) REFERENCES `unit` (`UNIT_NUMBER`)
);