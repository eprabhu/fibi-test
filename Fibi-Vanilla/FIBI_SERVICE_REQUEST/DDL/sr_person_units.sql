
DROP TABLE IF EXISTS `sr_person_units`;
CREATE TABLE `sr_person_units` (
  `SR_PERSON_UNIT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `SR_HEADER_ID` int(11) DEFAULT NULL,
  `SR_PERSON_ID` int(11) DEFAULT NULL,
  `LEAD_UNIT_FLAG` varchar(1) DEFAULT NULL,
  `UNIT_NUMBER` varchar(8) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`SR_PERSON_UNIT_ID`),
  KEY `SR_PERSON_UNITS_FK1` (`SR_PERSON_ID`),
  KEY `SR_PERSON_UNITS_FK2` (`UNIT_NUMBER`),
  CONSTRAINT `SR_PERSON_UNITS_FK1` FOREIGN KEY (`SR_PERSON_ID`) REFERENCES `sr_persons` (`SR_PERSON_ID`),
  CONSTRAINT `SR_PERSON_UNITS_FK2` FOREIGN KEY (`UNIT_NUMBER`) REFERENCES `unit` (`UNIT_NUMBER`)
) ;
