DROP TABLE IF EXISTS `sr_person_roles`;

CREATE TABLE `sr_person_roles` (
  `SR_PERSON_ROLE_ID` int(11) NOT NULL AUTO_INCREMENT,
  `SR_HEADER_ID` int(11) DEFAULT NULL,
  `PERSON_ID` varchar(45) DEFAULT NULL,
  `ROLE_ID` int(11) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `IS_SYSTEM_DERIVED_ROLE` varchar(1) DEFAULT NULL,
  PRIMARY KEY (`SR_PERSON_ROLE_ID`),
  KEY `SERVICE_REQUEST_PERSON_ROLES_FK1` (`PERSON_ID`),
  KEY `SERVICE_REQUEST_PERSON_ROLES_FK2` (`ROLE_ID`),
  KEY `SERVICE_REQUEST_PERSON_ROLES_FK3` (`SR_HEADER_ID`),
  CONSTRAINT `SERVICE_REQUEST_PERSON_ROLES_FK1` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`),
  CONSTRAINT `SERVICE_REQUEST_PERSON_ROLES_FK2` FOREIGN KEY (`ROLE_ID`) REFERENCES `role` (`ROLE_ID`)
) ;
