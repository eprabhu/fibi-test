DROP TABLE IF EXISTS `sr_module_mapping`;
CREATE TABLE `sr_module_mapping` (
  `SR_MODULE_MAPPING_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ASSOC_CONFIG_CODE` varchar(3) DEFAULT NULL,
  `CATEGORY_CODE` varchar(3) DEFAULT NULL,
  `TYPE_CODE` varchar(3) DEFAULT NULL,
  `IS_ACTIVE` varchar(1) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  PRIMARY KEY (`SR_MODULE_MAPPING_ID`),
  KEY `SR_MODULE_MAPPING_FK1` (`ASSOC_CONFIG_CODE`),
  KEY `SR_MODULE_MAPPING_FK2` (`CATEGORY_CODE`),
  KEY `SR_MODULE_MAPPING_FK3` (`TYPE_CODE`),
  CONSTRAINT `SR_MODULE_MAPPING_FK1` FOREIGN KEY (`ASSOC_CONFIG_CODE`) REFERENCES `assoc_config` (`ASSOC_CONFIG_CODE`),
  CONSTRAINT `SR_MODULE_MAPPING_FK2` FOREIGN KEY (`CATEGORY_CODE`) REFERENCES `sr_category` (`CATEGORY_CODE`),
  CONSTRAINT `SR_MODULE_MAPPING_FK3` FOREIGN KEY (`TYPE_CODE`) REFERENCES `sr_type` (`TYPE_CODE`)
) ;
