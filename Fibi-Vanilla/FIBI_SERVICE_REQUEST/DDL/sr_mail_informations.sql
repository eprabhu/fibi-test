
SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS `sr_mail_informations`;
CREATE TABLE `sr_mail_informations` (
  `MAIL_ID` int(11) NOT NULL AUTO_INCREMENT,
  `CLIENT_PARAM_ID` int(11) DEFAULT NULL,
  `MESSAGE_ID` varchar(500) DEFAULT NULL,
  `CONVERSATION_ID` varchar(500) DEFAULT NULL,
  `INLINE_ATTACHMENT_CONTENT_ID` mediumtext,
  `READ_COUNT` int(11) DEFAULT NULL,
  `READ_STATUS` varchar(10) DEFAULT NULL,
  `SR_HEADER_ID` int(11) DEFAULT NULL,
  PRIMARY KEY (`MAIL_ID`),
  KEY `SR_MAIL_INFORMATIONS_FK2` (`SR_HEADER_ID`),
  KEY `SR_MAIL_INFORMATIONS_FK1` (`CLIENT_PARAM_ID`),
  CONSTRAINT `SR_MAIL_INFORMATIONS_FK1` FOREIGN KEY (`CLIENT_PARAM_ID`) REFERENCES `mail_reader_client_parameter` (`CLIENT_PARAM_ID`),
  CONSTRAINT `SR_MAIL_INFORMATIONS_FK2` FOREIGN KEY (`SR_HEADER_ID`) REFERENCES `sr_header` (`SR_HEADER_ID`)
) ;
SET FOREIGN_KEY_CHECKS = 1;