
DROP TABLE IF EXISTS `sr_persons`;

CREATE TABLE `sr_persons` (
  `SR_PERSON_ID` int(11) NOT NULL AUTO_INCREMENT,
  `DEPARTMENT` varchar(200) DEFAULT NULL,
  `FULL_NAME` varchar(90) DEFAULT NULL,
  `PERSON_ID` varchar(40) DEFAULT NULL,
  `PERSON_ROLE_ID` int(11) DEFAULT NULL,
  `ROLODEX_ID` int(11) DEFAULT NULL,
  `SR_HEADER_ID` int(11) DEFAULT NULL,
  `PERCENTAGE_OF_EFFORT` decimal(5,2) DEFAULT NULL,
  `PI_FLAG` varchar(1) DEFAULT NULL,
  `DESIGNATION` varchar(255) DEFAULT NULL,
  `IS_PERSON_CERTIFIED` varchar(1) DEFAULT NULL,
  `PROJECT_ROLE` varchar(200) DEFAULT NULL,
  `IS_TRAINING_COMPLETED` varchar(16) DEFAULT NULL,
  `ACTION_FLAG` varchar(45) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`SR_PERSON_ID`),
  KEY `SR_PERSONS_FK1` (`PERSON_ROLE_ID`),
  CONSTRAINT `SR_PERSONS_FK1` FOREIGN KEY (`PERSON_ROLE_ID`) REFERENCES `eps_prop_person_role` (`PROP_PERSON_ROLE_ID`)
) ;
