DROP TABLE IF EXISTS `agreement_financial_detail`;

CREATE TABLE `agreement_financial_detail` (
  `FINANCIAL_DETAIL_ID` int(11) NOT NULL AUTO_INCREMENT,
  `AGREEMENT_REQUEST_ID` int(11) NOT NULL,
  `PROJECT_START_DATE` datetime DEFAULT NULL,
  `PROJECT_END_DATE` datetime DEFAULT NULL,
  `FA_RATE_TYPE_CODE` varchar(3) DEFAULT NULL,
  `FA_RATE_CLASS_CODE` varchar(3) DEFAULT NULL,
  `FA_RATE` decimal(15,2) DEFAULT NULL,
  `LOCATION` varchar(1) DEFAULT NULL,
  `COST_SHARE` decimal(15,2) DEFAULT NULL,
  `OBLIGATED_AMOUNT` decimal(15,2) DEFAULT NULL,
  `ANTICIPATED_AMOUNT` decimal(15,2) DEFAULT NULL,
  `DESCRIPTION` varchar(4000) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  PRIMARY KEY (`FINANCIAL_DETAIL_ID`),
  KEY `AGREEMENT_FINANCIAL_DETAILS_FK1` (`AGREEMENT_REQUEST_ID`),
  KEY `AGREEMENT_FINANCIAL_DETAILS_FK2` (`FA_RATE_CLASS_CODE`,`FA_RATE_TYPE_CODE`),
  CONSTRAINT `AGREEMENT_FINANCIAL_DETAILS_FK1` FOREIGN KEY (`AGREEMENT_REQUEST_ID`) REFERENCES `agreement_header` (`AGREEMENT_REQUEST_ID`),
  CONSTRAINT `AGREEMENT_FINANCIAL_DETAILS_FK2` FOREIGN KEY (`FA_RATE_CLASS_CODE`, `FA_RATE_TYPE_CODE`) REFERENCES `rate_type` (`RATE_CLASS_CODE`, `RATE_TYPE_CODE`)
) ;
