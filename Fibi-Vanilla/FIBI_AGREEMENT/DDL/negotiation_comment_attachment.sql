DROP TABLE IF EXISTS `negotiation_comment_attachment`;

CREATE TABLE `negotiation_comment_attachment` (
  `NEGOTIATION_COMMENT_ATTACH_ID` int(11) NOT NULL AUTO_INCREMENT,
  `FILE_NAME` varchar(300) DEFAULT NULL,
  `MIME_TYPE` varchar(255) DEFAULT NULL,
  `NEGOTIATION_ATTACHMENT_FILE_ID` varchar(255) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `NEGOTIATION_COMMENT_ID` int(11) NOT NULL,
  PRIMARY KEY (`NEGOTIATION_COMMENT_ATTACH_ID`),
  KEY `NEGOTIATION_COMMENT_ATTACH_FK1` (`NEGOTIATION_COMMENT_ID`),
  CONSTRAINT `NEGOTIATION_COMMENT_ATTACH_FK1` FOREIGN KEY (`NEGOTIATION_COMMENT_ID`) REFERENCES `negotiation_comment` (`NEGOTIATION_COMMENT_ID`)
) ;
