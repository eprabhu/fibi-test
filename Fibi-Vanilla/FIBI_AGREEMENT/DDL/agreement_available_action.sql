DROP TABLE IF EXISTS `agreement_available_action`;

CREATE TABLE `agreement_available_action` (
  `AVAILABLE_ACTION_ID` int(11) NOT NULL,
  `AGREEMENT_STATUS_CODE` varchar(3) DEFAULT NULL,
  `AGREEMENT_WORKFLOW_ACTION_ID` varchar(3) DEFAULT NULL,
  `IS_TERMINAL` varchar(1) DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  PRIMARY KEY (`AVAILABLE_ACTION_ID`),
  KEY `AGREEMENT_AVAILABLE_ACTION_FK1` (`AGREEMENT_STATUS_CODE`),
  KEY `AGREEMENT_AVAILABLE_ACTION_FK2` (`AGREEMENT_WORKFLOW_ACTION_ID`),
  CONSTRAINT `AGREEMENT_AVAILABLE_ACTION_FK1` FOREIGN KEY (`AGREEMENT_STATUS_CODE`) REFERENCES `agreement_status` (`AGREEMENT_STATUS_CODE`),
  CONSTRAINT `AGREEMENT_AVAILABLE_ACTION_FK2` FOREIGN KEY (`AGREEMENT_WORKFLOW_ACTION_ID`) REFERENCES `agreement_workflow_action` (`AGREEMENT_WORKFLOW_ACTION_ID`)
) ;