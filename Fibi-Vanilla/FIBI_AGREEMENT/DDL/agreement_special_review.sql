DROP TABLE IF EXISTS `agreement_special_review`;

CREATE TABLE `agreement_special_review` (
  `AGREEMENT_SPECIAL_REVIEW_ID` int(11) NOT NULL AUTO_INCREMENT,
  `APPLICATION_DATE` datetime DEFAULT NULL,
  `APPROVAL_DATE` datetime DEFAULT NULL,
  `APPROVAL_TYPE_CODE` varchar(3) DEFAULT NULL,
  `COMMENTS` longtext,
  `EXPIRATION_DATE` datetime DEFAULT NULL,
  `PROTOCOL_NUMBER` varchar(30) DEFAULT NULL,
  `PROTOCOL_STATUS_DESCRIPTION` varchar(200) DEFAULT NULL,
  `SPECIAL_REVIEW_CODE` varchar(3) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `AGREEMENT_REQUEST_ID` int(11) DEFAULT NULL,
  PRIMARY KEY (`AGREEMENT_SPECIAL_REVIEW_ID`),
  KEY `AGREEMENT_SPL_REVIEW_FK3` (`APPROVAL_TYPE_CODE`),
  KEY `AGREEMENT_SPL_REVIEW_FK1` (`AGREEMENT_REQUEST_ID`),
  KEY `AGREEMENT_SPL_REVIEW_FK2` (`SPECIAL_REVIEW_CODE`),
  CONSTRAINT `AGREEMENT_SPL_REVIEW_FK1` FOREIGN KEY (`SPECIAL_REVIEW_CODE`) REFERENCES `special_review` (`SPECIAL_REVIEW_CODE`),
  CONSTRAINT `AGREEMENT_SPL_REVIEW_FK2` FOREIGN KEY (`AGREEMENT_REQUEST_ID`) REFERENCES `agreement_header` (`AGREEMENT_REQUEST_ID`),
  CONSTRAINT `AGREEMENT_SPL_REVIEW_FK3` FOREIGN KEY (`APPROVAL_TYPE_CODE`) REFERENCES `sp_rev_approval_type` (`APPROVAL_TYPE_CODE`)
) ;
