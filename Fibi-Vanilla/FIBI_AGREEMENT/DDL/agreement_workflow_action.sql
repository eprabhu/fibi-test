DROP TABLE IF EXISTS `agreement_workflow_action`;

CREATE TABLE `agreement_workflow_action` (
  `AGREEMENT_WORKFLOW_ACTION_ID` varchar(3) NOT NULL,
  `DESCRIPTION` varchar(200) DEFAULT NULL,
  `IS_ACTIVE` varchar(1) DEFAULT NULL,
  `LOCATION_TYPE_CODE` varchar(3) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(40) DEFAULT NULL,
  `REVIEW_STATUS_CODE` varchar(3) DEFAULT NULL,
  `WORKFLOW_ACTION_TYPE` varchar(1) DEFAULT NULL,
  `AGREEMENT_STATUS_CODE` varchar(3) DEFAULT NULL,
  `ICON_CLASS_NAME` varchar(200) DEFAULT NULL,
  PRIMARY KEY (`AGREEMENT_WORKFLOW_ACTION_ID`),
  KEY `AGREEMENT_REVIEW_TYPE_FK2` (`REVIEW_STATUS_CODE`),
  KEY `AGREEMENT_REVIEW_TYPE_FK1` (`LOCATION_TYPE_CODE`),
  KEY `AGREEMENT_WORKFLOW_ACTION_FK3` (`AGREEMENT_STATUS_CODE`),
  CONSTRAINT `AGREEMENT_WORKFLOW_ACTION_FK1` FOREIGN KEY (`LOCATION_TYPE_CODE`) REFERENCES `negotiation_location_type` (`LOCATION_TYPE_CODE`),
  CONSTRAINT `AGREEMENT_WORKFLOW_ACTION_FK2` FOREIGN KEY (`REVIEW_STATUS_CODE`) REFERENCES `agreement_review_status` (`REVIEW_STATUS_CODE`),
  CONSTRAINT `AGREEMENT_WORKFLOW_ACTION_FK3` FOREIGN KEY (`AGREEMENT_STATUS_CODE`) REFERENCES `agreement_status` (`AGREEMENT_STATUS_CODE`)
) ;
