DROP TABLE IF EXISTS `agreement_keywords`;

CREATE TABLE `agreement_keywords` (
  `KEYWORD_ID` int(11) NOT NULL AUTO_INCREMENT,
  `SCIENCE_KEYWORD_CODE` varchar(15) DEFAULT NULL,
  `UPDATE_TIMESTAMP` datetime DEFAULT NULL,
  `UPDATE_USER` varchar(60) DEFAULT NULL,
  `AGREEMENT_REQUEST_ID` int(11) DEFAULT NULL,
  `KEYWORD` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`KEYWORD_ID`),
  KEY `AGREEMENT_KEYWORDS_FK1` (`AGREEMENT_REQUEST_ID`),
  KEY `AGREEMENT_KEYWORDS_FK2` (`SCIENCE_KEYWORD_CODE`),
  CONSTRAINT `AGREEMENT_KEYWORDS_FK1` FOREIGN KEY (`AGREEMENT_REQUEST_ID`) REFERENCES `agreement_header` (`AGREEMENT_REQUEST_ID`),
  CONSTRAINT `AGREEMENT_KEYWORDS_FK2` FOREIGN KEY (`SCIENCE_KEYWORD_CODE`) REFERENCES `science_keyword` (`SCIENCE_KEYWORD_CODE`)
) ;
